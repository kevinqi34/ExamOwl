<p><b>The Group 4: Experimental sciences</b> subjects of the International Baccalaureate Diploma Programme comprise the main scientific emphasis of this internationally recognized high school programme. They consist of seven courses, five of which are offered at both the Standard Level (SL) and Higher Level (HL): Chemistry, Biology, Physics, Design Technology, and, as of August 2012, Computer Science (previously a group 5 elective course) is offered as part of the Group 4 subjects. There are also two SL only courses: a transdisciplinary course, Environmental Systems and Societies, that satisfies Diploma requirements for Groups 3 and 4, and Sports, Exercise and Health Science (previously, for last examinations in 2013, a pilot subject ). Astronomy also exists as a school-based syllabus, particularly exciting if taken alongside Physics. Students taking two or more Group 4 subjects may combine any of the aforementioned.</p>
<p>The Chemistry, Biology, Physics and Design Technology courses will be updated for first teaching in September 2014, with syllabus updates (including a decrease in the number of options), a new internal assessment component similar to that of the Group 5 (mathematics) explorations, and "a new concept-based approach" dubbed "the nature of science". A new, standard level-only course will also be introduced to cater to candidates who do not wish to further their studies in the sciences, focusing on important concepts in Chemistry, Biology and Physics.</p>


<h3>Structure and assessment</h3>
<hr>
<p>All group 4 subjects (except computer science and environmental systems and societies; see below) follow roughly the same format. Each subject has its Subject Specific Core (SSC), i.e., material taught at both the standard and higher levels. Students sitting the Higher Level examination study the Additional Higher Level (AHL) material. Lastly, there is a list of options for each subject from which two are chosen. Higher Level students are sometimes unable to choose certain options that are available to Standard Level students because the AHL already covers it. Ideally, students choose the options based on their own abilities and preferences, but in practice the options are usually chosen by the school (based on the school's scientific facilities as well as the discretion of the instructor). Students spend one-quarter of the 150 hours of SL instruction (240 hours for HL; however, both numbers are merely recommendations and are not enforced) doing practical work in the laboratory. Group 4 subjects at the Standard Level are tailored for students who do not see themselves in further science instruction after leaving the programme.</p>
<p>Assessment of a Group 4 subject comprises the following:</p>

<ul>
<li>Internal assessment of the practical work (24%)</li>
<li>Paper 1 – multiple choice questions on the SSC (20%)</li>
<li>Paper 2 – free response questions on the SSC (32% at SL, 36% at HL)</li>
<li>Paper 3 – free response questions on the options (24% at SL, 20% at HL)</li>
</ul>
<p>At the Standard Level, the examinations are respectively 45 minutes, 1 hour and 15 minutes, and 1 hour long. At the Higher Level, they are 1 hour, 2 hours and 15 minutes, and 1 hour and 15 minutes long. Calculators are not permitted for Paper 1, but they (as well as a provided formula booklet and periodic table) are permitted for papers 2 and 3.</p>



