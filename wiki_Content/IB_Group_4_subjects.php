<p>All topics are compulsory (i.e. there are no options).</p>
<ul>
<li>Topic 1: Systems and models (5 hours)</li>
<li>Topic 2: The ecosystem (31 hours)</li>
<li>Topic 3: Human population, carrying capacity and resource use (39 hours)</li>
<li>Topic 4: Conservation and biodiversity (15 hours)</li>
<li>Topic 5: Pollution management (18 hours)</li>
<li>Topic 6: The issue of global warming (6 hours)</li>
<li>Topic 7: Environmental value systems (6 hours)</li>
</ul>
<p>The remaining 30 hours are derived from the internal assessment (practical work), making a total of 150 teaching hours.</p>

<p><b>Assessment</b></p>

<p>There are two external assessment components and one internal assessment component.</p>

<p><b>Calculators are required for both papers.</b></p>

<p>Calculators are <i>required</i> for both papers.</p>
<ul>
<li>Paper 1 (45 raw marks contributing 30% of the course, 1 hour) consists of short-answer and data-based questions.</li>
<li>Paper 2 (65 raw marks contributing 50% of the course, 2 hours) consists of:
<ul>
<li>Section A: Candidates are required to analyse and make reasoned and balanced judgements relating to a range of data on a specific unseen case study.</li>
<li>Section B: Candidates are required to answer two structured essay questions from a choice of four.</li>
</ul>
</li>
</ul>

<p><b>Internal assessment</b></p>

<p>Candidates will need to complete 30 hours of practical work throughout the course. Each of the three criteria - planning (Pl), data collection and processing (DCP) and discussion, evaluation and conclusion (DEC) - are assessed twice, while the fourth criterion - personal skills (PS) - is assessed summatively throughout the course. The maximum raw mark is 42, which contributes 20% of the course.</p>

<h3>Computer science (2014–2020)</h3>
<hr>

<p>The computer science course was recently updated and moved from Group 5 (as an elective course) to Group 4, becoming a full course, from first examinations in 2014. The structure and assessment of the course has changed to greater emphasize problem solving rather than Java program construction. The curriculum model for the course still differs from other Group 4 subjects however.</p>

<p><b>Topics</b></p>

<p>Standard Level candidates study the SL/HL core (80 hours) and the core of one option (30 hours), while Higher Level candidates study the SL/HL core (80 hours), HL extension (45 hours), an annually-issued case study (30 hours) and the whole of one option (30 + 15 hours). The remaining 40 hours for both Standard and Higher Level comes from the internal assessment component, for a total of 150 teaching hours at SL and 240 hours at HL.</p>

<p><b>SL/HL core</b></p>

<ul>
<li>Topic 1: System fundamentals (20 hours)</li>
<li>Topic 2: Computer organization (6 hours)</li>
<li>Topic 3: Networks (9 hours)</li>
<li>Topic 4: Computational thinking, problem-solving and programming (45 hours)</li>
</ul>

<p><b>HL extension</b></p>

<ul>
<li>Topic 5: Abstract data structures (23 hours)</li>
<li>Topic 6: Resource management (8 hours)</li>
<li>Topic 7: Control (14 hours)</li>
</ul>

<p><b>Options</b></p>

<ul>
<li>Option A: Databases (30/45 hours)</li>
<li>Option B: Modelling and simulation (30/45 hours)</li>
<li>Option C: Web science (30/45 hours)</li>
<li>Option D: Object-oriented programming (30/45 hours)</li>
</ul>

<p><b>Assessment</b></p>
<p>There are three external assessment components and two internal assessment components.</p>

<p><b>External assessment</b></p>

<p>Unlike other Group 4 subjects, calculators are <i>not permitted</i> in any computer science examination.</p>
<ul>
<li>Paper 1 (SL: 70 raw marks contributing 45% of the course, 1 hour 30 minutes; HL: 100 raw marks contributing 40% of the course, 2 hours 10 minutes) consists of:
<ul>
<li>Section A (about 30 minutes): Compulsory short answer questions on the SL/HL core and (for HL) the HL extension. Some questions are common to HL and SL. The maximum raw mark for this section is 25.</li>
<li>Section B (60 minutes for SL, 100 minutes for HL): 3 (SL) or 5 (HL) compulsory structured questions on the SL/HL core and the HL extension. Some questions may be common to HL and SL. The maximum raw marks for this section is 45 (SL) or 75 (HL).</li>
</ul>
</li>
<li>Paper 2 (SL: 45 raw marks contributing 25% of the course, 1 hour; HL: 65 raw marks contributing 20% of the course, 1 hour 20 minutes) consists of 2 to 5 (SL) or 3 to 7 (HL) compulsory questions based on the option studied. For HL, questions in section A (45 marks) consists of the core of the option, which may be common to the SL paper, and questions in section B (20 marks) are based on the extension of the option.</li>
<li>Paper 3 (HL only: 30 raw marks contributing 20% of the course, 1 hour) consists of 4 compulsory questions based on the pre-seen case study annually issued by the IBO.</li>
</ul>


<p><b>Internal assessment</b></p>

<p>Both SL and HL candidates must complete the following:</p>
<ul>
<li>A computational solution (30 hours, 34 raw marks). Candidates will need to develop a solution for a client to a problem or an unanswered question. This can be in the form of an entirely new system, or an addition of functionality to an existing system. Candidates will need to select, identify and work closely with an adviser, a third-party that can assist the candidate throughout the creation of the product. Candidates will need to complete an electronic HTML cover sheet (not assessed), the product and the documentation of the product (maximum 2000 words in total), including a 2 to 7-minute video showing the functionality of the product. The entire solution and documentation is marked against 5 criteria and is digitally compressed in a ZIP file and submitted for moderation.</li>
<li>The group 4 project (10 hours, 6 raw marks). Candidates will need to complete an interdisciplinary project with other science students. This is marked against the personal skills criterion.</li>
</ul>
<p>Both components carry a weightage of 30% (SL) or 20% (HL) of the computer science course.</p>

<h3>Group 4 project</h3>
<hr>

<p>All students of the Diploma Programme in any of these subjects, with the exception of environmental systems and societies, will compulsorily complete an inter-disciplinary and collaborative investigation called the Group 4 project. The Group 4 project assessment is included in the internal assessment marks. Students undertaking two or more group 4 courses will obtain the same mark for all of the courses.</p>


