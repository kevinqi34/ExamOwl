<p><b>Advanced Placement Spanish Language (often referred to as AP Spanish Language, AP Spanish V or simply AP Spanish)</b> is a course and examination offered by the College Board in the United States education system as part of the Advanced Placement Program.</p>

<h3>The Course</h3>
<hr>
<p>This course is primarily a comprehensive review of all previous knowledge pertaining to the Spanish language. This class builds upon the skills developed within introductory and intermediate Spanish classes by applying each skill to a specific, contemporary context (health, education, careers, literature, history, family, relationships, and environment being common themes). Thus, the students strive to refine their skills in writing, reading, speaking, and understanding spoken Spanish. Students concentrate on developing proficiency in such skills specifically in preparation for the AP Spanish Language examination. In addition, this course will emphasize mastery of linguistic competencies at a very high level of proficiency.</p>
<p>Despite the best attempts by the College Board the AP Spanish Language curriculum is very fluid. Individual teachers can choose to present as much or as little information as possible. Because teachers inherently have different methods of pedagogy, issues arise that pertain to the necessity of a standardized Spanish curriculum for the exam. Because the Spanish Language is so eclectic and can be tested in a plethora of manners, a more solidified curriculum covering specific vocabulary, verb forms and usages, expressions, and other facets of the language may be required in the future.</p>
<p>While some students may be concerned about their ability to demonstrate proficiency in an assessment that native speakers of Spanish also take, only the scores of students who study Spanish as a second language are factored when creating the distribution curve of scores 1-5. Native speakers or heritage language speakers of Spanish are then compared to non-native distribution and assigned a score accordingly.</p>


<h3>The Exam</h3>
<hr>
<p>As of May 2007, the exam is divided into two sections with several parts. Section one contains sections of listening comprehension and reading comprehension, in which the student must listen to interviews, broadcasts, or other spoken materials, or read several journalistic or literary passages and then answer multiple-choice questions about them. The reading passages may include a visual component or a web page.</p>
<p>Section two contains a portion devoted to grammar and a portion devoted to speaking skills. In the grammar section, the student is given a paragraph or sentence in which some words are missing, and must insert the correct verb form, adjective, pronoun, or participle from a given root word. However, as of the 2009 test administration, the entire grammar portion has been removed to conserve time. The weight of the interpersonal writing section has been increased to 10% to compensate for this change. Students must also write an informal essay, such as a letter. A formal writing component takes the shape of a document-based question. Students must use documents as well as listen to a recording to give a written answer to the question. In the informal speaking section, students are expected to interact to a recorded dialogue, during which they have 20 seconds to answer each section. Students are also asked to give a formal oral presentation over a written document and a recording which they have 2 minutes to answer.</p>
<p>The test was approximately three hours in duration, but as a time-saving measure, the instructions for the 2008 exam will be read only in Spanish as an attempt to decrease time to 2½ hours. (Printed instructions in both English and Spanish will continue to be provided.)</p>
<p>Note: Test centers often use portable stereo systems for audio sections and tape recorder devices to record test taker responses for the dialogue and oral presentation sections. Other sites may rely on digital recording and submission of speaking samples.</p>

