<p><b>The Group 3: Individuals and societies (previously Humanities)</b> subjects of the IB Diploma Programme consist of nine courses offered at both the Standard level (SL) and Higher level (HL): Business and management, Economics, Geography, History, Information technology in a global society (ITGS), Philosophy, Psychology, Social and cultural anthropology, and World religions (SL only). There is also a transdisciplinary course, Environmental systems and societies (SL only), that satisfies Diploma requirements for Groups 3 and 4, and a pilot course for Global politics that is currently offered at schools participating in the pilot program.</p>

<h3>History (2010-2016)</h3>
<hr>

<p>History is the process of recording, reconstructing and interpreting the past through investigation of sources. IB DP History teaches candidates to interpret and critically evaluate these sources and allows them to understand and appreciate the culture and context of those living in other periods of time.</p>
<p><b>Syllabus</b></p>
<p>Candidates can follow one of the two routes for IB DP History, depending on availability in the school. Regardless of which route is taken, SL and HL candidates must study one "prescribed subject" (changes every syllabus update - 7 years) out of a selection of five (two for route 1 and three for route 2) in depth. Candidates will also need to study two topics out of ten (five for each route) in detail. For route 2, each topic must also cover a different region. Finally, HL candidates will need to study three sections out of twelve within one option (from a choice of five).</p>

<p><b>SL/HL core</b></p>

<p>All candidates must study one prescribed subject and two topics for 130 hours.</p>

<p><b>Route 1: History of Europe and the Islamic world</b></p>

<p>The first route involves the study of medieval Europe and Arab, and the origins and propagation of the Islamic religion between the years 600 and 1450.</p>

<p><b>Prescribed subjects</b></p>

<ul>
<li>Prescribed subject 1: The origins and rise of Islam c500-661 (40 hours)</li>
<li>Prescribed subject 2: The kingdom of Sicily 1130-1302 (40 hours)</li>

</ul>

<p><b>Topics</b></p>

<ul>
<li>Topic 1: Dynasties and rulers (45 hours)</li>
<li>Topic 2: Society and economy (45 hours)</li>
<li>Topic 3: Wars and warfare (45 hours)</li>
<li>Topic 4: Intellectual, cultural and artistic developments (45 hours)</li>
<li>Topic 5: Religion and the state (45 hours)</li>
</ul>

<p><b>Route 2: 20th century world</b></p>

<p>The second route involves the study of 20th century world history.</p>
<p><b>Prescibed Subjects</b></p>
<ul>
<li>Prescribed subject 1: Peacemaking, peacekeeping—international relations 1918-36 (40 hours)</li>
<li>Prescribed subject 2: The Arab-Israeli conflict 1945-79 (40 hours)</li>
<li>Prescribed subject 3: Communism in crisis 1976-89 (40 hours)</li>
</ul>

<p><b>Topics</b></p>
<ul>
<li>Topic 1: Causes, practices and effects of wars (45 hours)</li>
<li>Topic 2: Democratic states—challenges and responses (45 hours)</li>
<li>Topic 3: Origins and development of authoritarian and single-party states (45 hours)</li>
<li>Topic 4: Nationalist and independence movements in Africa and Asia and post-1945 Central and Eastern European states (45 hours)</li>
<li>Topic 5: The Cold War (45 hours)</li>
</ul>


<p><b>HL options</b></p>
<p>HL candidates must study one option from the following for 90 hours. Route 1 candidates must study option 1 and route 2 candidates may choose one from options 2 to 5. Each option has twelve sections. Candidates must study three sections.</p>
<ul>
<li>Option 1: Aspects of the history of medieval Europe and the Islamic world (90 hours)</li>
<li>Option 2: Aspects of the history of Africa (90 hours)</li>
<li>Option 3: Aspects of the history of the Americas (90 hours)</li>
<li>Option 4: Aspects of the history of Asia and Oceania (90 hours)</li>
<li>Option 5: Aspects of the history of Europe and the Middle East (90 hours)</li>
</ul>
<p>The remaining 20 hours for both SL and HL candidates comes from the internal assessment component, making a total of 150 teaching hours for SL and 240 hours for HL.</p>


<p><b>Assessment</b></p>
<p>There are three assessment components at SL and four at HL.</p>
<p><b>External assessment</b></p>

<ul>
<li>Paper 1 (25 marks weighed at 30% for SL and 20% for HL, 1 hour) - Candidates answer four structured, short-answer questions on the prescribed subject studied. This paper is common to both SL and HL.</li>
<li>Paper 2 (40 marks weighed at 45% for SL and 25% for HL, 1 hour 30 minutes) - Candidates must answer two extended response questions, one on each topic studied. Each topic has a selection of 6 questions. This paper is common to both SL and HL.</li>
<li>Paper 3 (HL only: 60 marks weighed at 35% of the course, 2 hours 30 minutes) - Candidates must answer three extended response questions, one on each of the three sections of the HL option studied. There are two questions set for each section. Each option has a separate examination paper.</li>
</ul>

<p>External assessment accounts for 75% of the course grade at SL and 80% at HL.</p>

<p><b>Internal assessment</b></p>

<ul>
<li>Historical investigation (25 marks weighed at 25% for SL and 20% for HL, 20 hours) - Candidates research and write a historical investigation of 1500 to 2000 words. The topic can be freely chosen from any part of the syllabus. The investigation is internally marked and externally assessed.</li>
</ul>

<p>Internal assessment accounts for the remaining 25% of the course grade at SL and 20% at HL.</p>

<h3>Economics SL & HL</h3>
<hr>

<p>The syllabus of the Economics course is divided into four sections – microeconomics, macroeconomics, international economics, and development economics – all of which receive approximately equal weight. All sections must be studied by all candidates, and questions of all will be posed in examinations. The Diploma programme Economics course is noted for focusing more on development than any other economics course at a pre-university level, and this is all part of the IB programmes' international perspective.</p>
<p>The final exams consist of three papers for HL and two for SL. Paper 1 has consisted of multiple choice questions but has now been changed to answering one question from microeconomics and one question from macroeconomics, for each section one question is worth 10 marks and the other is worth 15 marks. Paper 2 involves answering two data response questions, one from international economics and the other from development economics, each data response question is worth 20 marks. Paper 3 involves calculations (only HL is required to do this paper). Internal assessment includes three commentaries of current news items involving the use of economic concepts and terminology. The time allowed for each exam is a strict limit of 90 minutes each for Paper 1 and Paper 2 (both SL and HL) and 1 hour for Paper 3 (HL only).</p>

<h3>Psychology SL & HL</h3>
<hr>

<p>The focus of this course is the systematic study of behavior and mental process. The program studies three main perspectives as influences on human behavior: the biological, the cognitive and the sociocultural. It includes the examination of optional topics that include health psychology, abnormal psychology, developmental psychology, sports psychology and psychology of human relationships. Finally all students have to carry out their own experimental study as an internal assessment. Students at higher level study two options whereas standard level students study only one.</p>

<p>Each of the perspectives should be explored using the following four compulsory topics:</p>
<ul>
<li>development and cultural contexts</li>
<li>framework</li>
<li>methodologies</li>
<li>application.</li>
</ul>
<p>The aims of the psychology course at HL and at SL are to:</p>
<ul>
<li>interpret and/or conduct psychological research to apply the resulting knowledge for the benefit of human beings</li>
<li>ensure that ethical practices and responsibilities are implemented in psychological inquiry</li>
<li>develop an understanding of the biological, social and cultural influences on human behaviour</li>
<li>develop an understanding of different theoretical processes that are used to interpret behaviour, and to be aware of how these processes lead to the construction and evaluation of psychological theories</li>
<li>develop an awareness of how applications of psychology in everyday life are derived from psychological theories</li>
<li>develop an appreciation of the eclectic nature of psychology</li>
<li>understand and/or use diverse methods of psychological inquiry.</li>
</ul>

<p>First, students must choose an experiment to replicate. When conducting the experiment, the IB demands that certain ethical guidelines be followed. After the experiment has been completed, a written report must be produced detailing the experiment. The students are required to The external assessment, which is administered in May or November; the second year of the course is specially focused to the development of this assessment where the students are introduced to a variety of studies and the student is expected to draw connections between them. The test is divided into two parts, which are referred to as "papers". Using the internal and external assessment, IB calculates a grade value of one through seven.</p>

<h3>Philosophy SL & HL</h3>
<hr>

<p>Philosophy is offered both as a standard and higher level Group 3 subject. It consists of both internal assignment (philosophical approach to an essay on current topic) and 2 (3 on higher level) externally assessed exam papers on core and optional topics.</p>

<h3>Information technology in a global society (ITGS) SL & HL</h3>
<hr>

<p>The IB Diploma Programme information technology in a global society (ITGS) course is the study and evaluation of the impacts of information technology (IT) on individuals and society. It explores the advantages and disadvantages of the access and use of digitized information at the local and global level. ITGS provides a framework for the student to make informed judgments and decisions about the use of IT within social contexts.</p>
<p>Requirements for SL&#160;:</p>
<ul>
<li>External assessment: 2 exam papers (3 hours), counts 70% of the final mark</li>
<li>Internal assessment SL 30%: The requirement of the project is to develop an original IT solution to a real problem for a specified client.</li>
</ul>
<p>Requirements for HL:</p>
<ul>
<li>External assessment: 3 exam papers (4.45 hours), counts 80% of the final mark</li>
<li>Internal assessment HL 20%: The requirement of the project is to develop an original IT solution to a real problem for a specified client.</li>
</ul>
<p>Practical computer work is conducted while researching for the project at Standard Level. As of exams starting in 2012 both HL and Sl students will take the Project.</p>


<h3>Geography (2011-2017)</h3>
<hr>


<p>Geography involves the study and investigation of human relationships with the environment.</p>
<p><b>Syllabus</b></p>

<p>SL candidates study three themes - the core theme and two optional themes, while HL candidates study five themes - the core theme, three optional themes and the HL extension.</p>

<p><b>SL/HL core</b></p>

<p>All candidates must study this theme.</p>

<p>Theme: Patterns and change (70 hours)</p>


<ul>
<li>Topic 1: Populations in transition (19 hours)</li>
<li>Topic 2: Disparities in wealth and development (16 hours)</li>
<li>Topic 3: Patterns in environmental quality and sustainability (19 hours)</li>
<li>Topic 4: Patterns in resource consumption (16 hours)</li>
</ul>

<p><b>Options</b></p>


<p>SL candidates must study two of the following seven themes (60 hours), while HL candidates must study three (90 hours). Teachers may teach more themes than prescribed so that the candidates have a greater freedom of choice in Paper 2, since all options are set on the same paper.</p>


<ul>
<li>Option A: Freshwater - issues and conflicts (30 hours)</li>
<li>Option B: Oceans and their coastal margins (30 hours)</li>
<li>Option C: Extreme environments (30 hours)</li>
<li>Option D: Hazards and disasters - risk assessment and response (30 hours)</li>
<li>Option E: Leisure, sport and tourism (30 hours)</li>
<li>Option F: The geography of food and health (30 hours)</li>
<li>Option G: Urban environments (30 hours)</li>
</ul>

<p><b>HL extension</b></p>
<p>HL candidates must study this theme.</p>
<p>Theme: Global interactions (60 hours)</p>
<ul>
<li>Topic 1: Measuring global interactions (4 hours)</li>
<li>Topic 2: Changing space - the shrinking world (12 hours)</li>
<li>Topic 3: Economic interactions and flows (8 hours)</li>
<li>Topic 4: Environmental change (8 hours)</li>
<li>Topic 5: Sociocultural exchanges (8 hours)</li>
<li>Topic 6: Political outcomes (10 hours)</li>
<li>Topic 7: Global interactions at the local level (10 hours)</li>
</ul>



<p><b>Assessment</b></p>
<p>There are three assessment components at SL and four at HL.</p>

<p>External Assessment</p>

<ul>
<li>Paper 1 (60 marks weighing 40% of the course for SL and 25% for HL, 1 hour 30 minutes) - Both SL and HL candidates sit the same paper assessing their knowledge on the core theme.
<ul>
<li>Section A (45 marks; questions 1-4) consists of four compulsory short-answer questions, one on each of the four topics in the theme. Command terms indicate the depth of the answer required (e.g. <i>evaluate</i> indicates that more depth is required than <i>define</i>).</li>
<li>Section B (15 marks; questions 5-7) consists of three extended response questions that require more in-depth treatment than in Section A, and are required to answer one. Questions are based on the core theme but may link with the optional themes as well.</li>
</ul>
</li>
<li>Paper 2 (40 marks at SL and 60 marks at HL, both weighed to 35%, 1 hour 20 minutes at SL, 2 hours at HL) - This paper assesses knowledge on the optional themes studied. Essentially, SL and HL students sit the same paper except that SL candidates answer two questions and HL candidates answer three from the themes studied, for 20 marks each. The paper consists of 14 questions, two on each theme, and may also come with a resources booklet for certain questions. Each question has at least three parts - earlier parts are short-answer questions which may or may not require depth. The last part is a 10-mark extended response question which more depth is required.</li>
<li>Paper 3 (HL only: 25 marks weighing 20% of the course, 1 hour) - HL candidates are assessed on the HL extension in this paper, but knowledge of the core theme is assumed. Students choose one question to answer out of a choice of three. Each question has two parts worth 10 and 15 marks respectively. Both parts require essay-length and in-depth writing. The first part tests understanding and application, while the second tests synthesis and evaluation.</li>
</ul>
<p>External assessment accounts for 75% of the grade for the entire course at SL, and 80% of that at HL.</p>

<p><b>Internal Assessment</b></p>
<ul>
<li>Fieldwork (30 marks weighing 25% of the course for SL and 20% for HL, 20 hours) - All candidates are required to complete a fieldwork investigation based on one or more themes in the syllabus and write a 2500-word report based on the collection of primary data, processing the data and evaluating the fieldwork. Secondary data may be collected but only play a smaller part in the fieldwork. Reports are marked according to seven criteria by the teacher and then sent for external moderation. The report demands the same from both SL and HL.</li>
</ul>
<p>Internal assessment accounts for the remaining 25% of the grade at SL and 20% at HL.</p>

<h3>Anticipated Subjects</h3>
<hr>

<p>In some cases, Standard level subjects can be studied in one year, as opposed to the two years for Higher level subjects.This gives students more study time in their final year of school, as well as the option to spend more hours on their higher level subjects. All anticipated subjects are studied at standard level.</p>



