<p><b>Advanced Placement Chinese Language and Culture (commonly known as AP Chinese Language and Culture or AP Chinese)</b> is a course offered by the College Board as a part of the Advanced Placement Program in the United States. It requires proficiencies throughout the Intermediate range as described in the American Council on the Teaching of Foreign Languages (ACTFL) Proficiency Guidelines. The course interweaves language and culture learning and is conducted mostly in Chinese. The first AP Chinese test was administered on May 9, 2007. Many native Chinese speakers take the exam, leading to an overwhelming majority obtaining the maximum score.</p>

<h3>Format of exam</h3>
<hr>
<p>The AP Chinese Language and Culture Exam is approximately 2 hours and 15 minutes in length . It assesses Interpersonal, Interpretive, and Presentational communication skills in Mandarin Chinese, along with knowledge of Chinese culture </p>
<p>Section I consists of multiple-choice questions.</p>
<ul>
<li>Part A: Listening</li>
<li>Part B: Reading</li>
</ul>
<p>Section II, the free-response section, requires you to produce written and spoken responses.</p>
<ul>
<li>Part A: Writing</li>
<li>Part B: Speaking</li>
</ul>




