<p><b>Advanced Placement (AP) Physics 1</b>, along with AP Physics 2, are year-long AP courses designed by the College Board to replace AP Physics B in the 2014 - 2015 school year. The courses will teach the same general curriculum as AP Physics B, but instead split the course into two and cover more information, specifically rotational mechanics. The courses were formed through collaboration between current Advanced Placement teachers and the College Board, with the guidance of the National Research Council and the National Science Foundation. Similar to AP Physics C the course is said to cover the material of a first semester university undergraduate physics course offered at a typical American university, but with an algebra based curriculum as opposed to AP Physics C calculus based curriculum.</p>

<h3>History</h3>
<hr>

<p>The first AP Physics 1 classes began in the 2014-2015 school year, with the first AP exams to be administered in May 2015. The College Board's professional development programs for teachers began including information about AP Physics 1 in 2013, and comprehensive institutes and workshops were available as of the summer prior to the first exam administration.</p>
<p>Criticism has arisen since the creation of the exam, as it receives the same amount of credit as AP Physics C, whilst providing a much easier curriculum. However, the AP Physics C exam correlates to common calculus-based physics courses for engineers and physics majors. The AP Physics 1 exam correlates to algebra-based courses for students not entering physics or engineering fields.</p>



<h3>Curriculum</h3>
<hr>

<p>AP Physics 1 is an algebra-based, introductory college-level physics course that explores topics such as Newtonian mechanics (including rotational motion); work, energy, and power; mechanical waves and sound; and introductory, simple circuits. The College Board published a "Curriculum Framework" which includes the 7 "big ideas" on which the new AP Physics courses will be based, along with "Enduring Understandings" that students will be expected to acquire within each of the big ideas.</p>
<p>Questions for the exam are to be constructed with direct reference to items in the Curriculum Framework. Students' understanding of each topic is tested with reference to multiple skills -- that is, questions require students to use quantitative, semi-quantitative, verbal, and experimental reasoning in every content area.</p>

