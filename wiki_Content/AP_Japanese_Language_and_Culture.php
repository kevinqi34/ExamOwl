<p><b>Advanced Placement Japanese Language and Culture (also known as AP Japanese Language and Culture, AP Jap, or AP Japanese)</b> is a course offered by the College Board as part of the Advanced Placement Program in the United States. It is intended to give students a thorough background in the Japanese language and Japanese social customs. The class was first given as a certified College Board program in the 2006-07 school year. Preparations for the corresponding test were made, but the complex computer and internet requirements were not fully sorted out by administration time, and the exam was not given in some areas.</p>

<h3>Exam Outline</h3>
<hr>
<p>Similar to the AP Chinese Language and Culture test, the exam is taken on a computer, requiring participants to read, write, and speak in Japanese, utilizing computers, headphones, and microphones. The 2012 exam was split mainly into two sections.</p>
<p><b>Section I</b></p>
<p>Students must answer multiple choice questions in response to audio prompts and short texts.</p>
<p><b>Section II</b></p>
<p>Students compose free-response answers in text-chat messages with 90 seconds to respond, and short responses with 20 minutes to respond. Audio conversations with 20-second responses, and a two-minute presentation complete the exam.</p>



<h3>Grade Distribution</h3>
<hr>
<p>In the 2012 administration, 2,177 students took the exam, with a mean score of 3.64. 1,192 students indicated themselves as non-native speakers, who did not use Japanese on a regular basis. The mean score for this group was 2.88</p>
