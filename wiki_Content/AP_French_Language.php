<p><b>Advanced Placement French Language and Culture (also known as AP French Lang, FLAP, AP French Language or AP French)</b> is a course offered by the College Board to high school students in the United States as an opportunity to earn placement credit for a college-level French course. The AP French Language test is widely compared to a final examination for a French 301 college course. Enrollment requirements for AP French Language differ from school to school, but students wishing to enter it should have a good command of French grammar and vocabulary as well as prior experience in listening, reading, speaking, and writing French and a holistic cultural knowledge of francophone regions. The AP French Language and Culture exam underwent a change beginning in May 2012 designed to better test interpretive, interpersonal, and presentational communication skills.</p>

<h3>The Exam</h3>
<hr>

<p>The AP French Language and Culture class culminates in an exam to test students’ proficiency.</p>
<p>The exam is approximately 3 hours in length with a 95-minute multiple choice section and an 85-minute free response section. It measures the student's French ability in two general sections, each of which counts as one half of the final grade. Interpretive communication is tested in a multiple-choice section; interpersonal and presentational communication are tested in a free response section.</p>

<p><b>Section I: Multiple Choice</b></p>

<p>Part A consists of a variety of authentic print materials (such as journalistic and literary texts, announcements, advertisements, letters, maps, and tables) with a series of multiple-choice questions.</p>
<p>Part B consists of a variety of authentic audio materials, including interviews, podcasts, public service announcements, conversations, and brief presentations. This section is divided into two subsections. In the first, the audio texts are paired with print materials; the second consists solely of audio texts. Students are allowed some time to read the preview and skim the questions before listening to the audio. All audio texts are played twice.</p>

<p><b>Section II: Free Response</b></p>
<p>In the writing portion, students demonstrate their ability to write in the interpersonal mode by replying to an e-mail message, and in the presentational mode by writing a persuasive essay based on three sources that present different viewpoints on a topic. The three sources are an article, a table or graphic, and an audio piece that is played twice. Students have 40 minutes to write the essay, during which they may have access to the print sources and any notes they took on the audio.</p>
<p>In the speaking portion, students first demonstrate the interpersonal mode. The speaking portion assesses speaking in the Interpersonal mode by responding to questions as part of a simulated conversation; they first see a preview of the conversation, including an outline of each exchange. In the second part of this portion, they demonstrate the presentational mode by giving a 2-minute presentation in response to a prompt on a cultural topic. The presentation is to compare cultural features of the student's own community with those in an area of the French-speaking world. Students are encouraged to cite examples from materials they have read, viewed, and listened to, as well as from personal experiences and observations.</p>




<h3>Grade Distribution</h3>
<hr>
<p>In the 2012 administration, 19,769 students took the exam, with a mean score of 3.47. 15,041 students identified themselves as non-native speakers who did not use French on a regular basis. The mean score for this group was 3.21.</p>
