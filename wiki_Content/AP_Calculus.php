<p><b>Advanced Placement Calculus (also known as AP Calculus, AP Calc AB / AP Calc BC, or AP Calc)</b> is used to indicate one of two distinct Advanced Placement courses and examinations offered by College Board in calculus: AP Calculus AB and AP Calculus BC.</p>







<h3>AP Calculus AB</h3>
<hr>


<p>AP Calculus AB is an Advanced Placement calculus course taken by high school students. The course is traditionally taken after precalculus and is the first calculus course offered at most schools except for the regular calculus class. The Pre-Advanced Placement pathway for math will help prepare students for further Advanced Placement classes and exams.</p>

<p><b>Purpose</b></p>

<p>An AP course in calculus consists of a full high school academic year of work that is comparable to calculus courses in colleges and universities. It is expected that students who take an AP course in calculus will seek college credit, college placement, or both, from institutions of higher learning.</p>

<p>The AP Program includes specifications for two calculus courses and the exam for each course. The two courses and the two corresponding exams are designated as Calculus AB and Calculus BC. Calculus AB can be offered as an AP course by any school that can organize a curriculum for students with advanced mathematical ability.</p>

<p><b>Course Content</b></p>

<p>The material includes the study and application of differentiation and integration, and graphical analysis including limits, asymptotes, and continuity. An AP Calculus AB course is typically equivalent to one semester of college calculus. More specifically, the topics are[</p>

<ul>
<li>Analysis of graphs (predicting and explaining behavior)</li>
<li>Limits of functions (one and two sided)</li>
<li>Asymptotic and unbounded behavior</li>
<li>Continuity</li>
<li>Derivatives
<ul>
<li>Concept</li>
<li>At a point</li>
<li>As a function</li>
<li>Applications</li>
<li>Higher Order derivatives</li>
<li>Techniques</li>
</ul>
</li>
<li>Integrals
<ul>
<li>Interpretations</li>
<li>Properties</li>
<li>Applications</li>
<li>Techniques</li>
<li>Numerical approximations</li>
</ul>
</li>
<li>Fundamental theorem of calculus</li>
<li>Antidifferentiation</li>
<li>L'Hôpital's rule, starting in the 2016-17 school year.</li>
</ul>



<h3>AP Calculus BC</h3>
<hr>


<p><b>Purpose</b></p>

<p>Calculus BC is a full-year course in the calculus of functions of a single variable. It includes all topics covered in Calculus AB plus additional topics...Students who take an AP Calculus course should do so with the intention of placing out of a comparable college calculus course.</p>

<p><b>Course Content</b></p>

<p>AP Calculus BC includes all of the topics covered in AP Calculus AB, as well as convergence tests for series, Taylor series, the use of parametric equations, polar functions, including arc length in polar coordinates, calculating curve length in parametric and function equations, L'Hôpital's rule, integration by parts, improper integrals, Euler's method, differential equations for logistic growth, and using partial fractions to integrate rational functions.</p>


<h3>Exam</h3>
<hr>

<p><b>Growth</b></p>
<p>Between 1990 and 2004, the number of students taking the AP Calculus exams has increased more than threefold. The exams are now taken by more than 250,000 students each year. The College Board intentionally schedules the AP Calculus AB exam at the same time as the AP Calculus BC exam in order to make it impossible for a student to take both tests in the same academic year, though the College Board does not make Calculus AB a pre-requisite class for Calculus BC. Some schools do this, though many others only require precalculus as a prerequisite for Calculus BC.</p>


<p><b>Format</b></p>
<p>The structures of the AB and BC exams are identical. Both exams are three hours and fifteen minutes long, comprising a total of 45 multiple choice questions and six free response questions.</p>
