<p>The two parts of the Multiple-Choice section are timed and taken independently; students may work on the Section II Part A during the time for Section II Part B but are not allowed to resume using a calculator. The Free-Response section is one-and-a-half hours long. New to the exam in 2011, the calculator-required free response section contains only 2 questions, while the non-calculator section contains 4 questions. Students are required to put away their calculators after 30 minutes have passed during the Free-Response section, and only at that point may begin Section II Part B. However, students may continue to work on Section II Part A during the entire Free-Response time, although without a calculator during the later half.</p>


<p><b>Scoring</b></p>
<p>The Multiple-Choice section is scored by computer, with a correct answer receiving 1 point, a blank answer receiving 0 points, and an incorrect answer costing no points as a new change made by AP Central. This total is multiplied by 1.2 to calculate the adjusted multiple-choice score.</p>
<p>The Free-Response section is hand-graded by hundreds of educators each June. The raw score is then added to the adjusted multiple choice score to receive a composite score. This total is compared to a composite-score scale for that year's exam and converted into an AP score of 1 to 5.</p>
<p>Prior to 2013, students typically received this score report by mail in mid-July of the year they took the test. As of 2013, the College Board will not send the scores by mail, but instead the student taking the test will have to create a username and a password as well as input their AP code or student ID number in order to see their scores. The scores will be available sometime in July. Alternately, they can receive their scores by phone as early as June 27 for a fee of $8 (although the College Board only officially recognizes July 1 as the first available date to receive grades by phone).</p>
<p>For the Calculus BC exam, an AB sub-score is included in the score report to reflect their proficiency in the fundamental topics of introductory calculus. The AB sub-score is based on the correct number of answers for questions pertaining to AB-material only.</p>


