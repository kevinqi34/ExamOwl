<p><b>Advanced Placement (AP)</b> is a program in the United States and Canada, created by the College Board, which offers college-level curricula and examinations to high school students. American colleges and universities often grant placement and course credit to students who obtain high scores (3 and above) on the examinations. The AP curriculum for each of the various subjects is created for the College Board by a panel of experts and college-level educators in that field of study. For a high school course to have the AP designation, the course must be reviewed by the College Board to certify that it satisfies the AP curriculum. If the course is approved, the school may use the AP designation and the course will be publicly listed on the AP Course catalogue.</p>

<p><b>Quick Stats:</b> In 2014, the most taken AP exam was English Language and Composition with 505,244 students, and the least taken AP exam was Japanese Language and Culture with 2,311 students.</p>

<h3>Scoring</h3>

<hr>

<p>AP tests are scored on a 1 to 5 scale as follows:</p>

<ul>
<li>5 – Extremely well qualified</li>
<li>4 – Well qualified</li>
<li>3 – Qualified</li>
<li>2 – Possibly qualified</li>
<li>1 – No recommendation</li>
</ul>

<p>Grading AP exams is a long and complicated process. The multiple choice component of the exam is scored by computer, while the free response and essay portions are scored by trained Readers at the AP Reading each June. The scores on various components are weighted and combined into a raw Composite Score. The Chief Reader for each exam then decides on the grade cutoffs for that year's exam, which determine how the Composite Scores are converted into the final grades. During the process a number of reviews and statistical analyses are performed to ensure that the grading is reliable. The overall goal is for the grades to reflect an absolute scale of performance which can be compared from year to year.</p>

<p>Some colleges use AP test scores to exempt students from introductory coursework, others use them to place students in higher designated courses, and some do both. Each college's policy is different (see link below), but most require a minimum score of 4 or 3 to receive college credit. Typically this appears as a "CR" grade on the college transcript, although some colleges and universities will award an A grade for a 5 score. Some countries, such as Germany, that do not offer general admission to their universities and colleges for holders of an American high school diploma without lengthy preparatory courses will directly admit students who have completed a specific set of AP tests, depending on the subject they wish to study there.</p>

<p>In addition, completing AP courses help students qualify for various types of scholarships. According to the College Board, 31 percent of colleges and universities look at AP experience when making scholarship decisions.</p>

<p>Beginning with the May 2011 AP Exam administration, the College Board changed the scoring method of AP Exams. Total scores on the multiple-choice section are now based on the number of questions answered correctly. Points are no longer deducted for incorrect answers and, as was the case before, no points are awarded for unanswered questions. However, scoring requirements have also been increased.</p>

<h3>Score Reporting</h3>

<p>Starting with the May 2013 AP Examination Administration, the College Board launched an Internet-based score reporting service. Students can use their 2013 AP Number or Student Number (if one was indicated), along with a College Board Account, to access current and previous years' exam scores. This system can also be used to send scores to colleges and universities for which a 4-digit institutional code is assigned.</p>

<h3>History</h3>
<hr>
<p>After the end of World War II, the Ford Foundation created a fund that supported committees studying education. The program, which was then referred to as the "Kenyon Plan," was founded and pioneered at Kenyon College in Gambier, Ohio, by the then college president Gordon Chalmers. The first study was conducted by three prep schools—the Lawrenceville School, Phillips Academy and Phillips Exeter Academy—and three universities—Harvard University, Princeton University and Yale University. In 1952 they issued the report General Education in School and College: A Committee Report which recommended allowing high school seniors to study college level material and to take achievement exams that allowed them to attain college credit for this work. The second committee, the Committee on Admission with Advanced Standing, developed and implemented the plan to choose a curriculum. A pilot program was run in 1952 which covered eleven disciplines.</p>

<p>The College Board, a non-profit organization based in New York City, has run the AP program since 1955. From 1965 to 1989, Harlan Hanson was the director of the Advanced Placement Program. It develops and maintains guidelines for the teaching of higher level courses in various subject areas. In addition, it supports teachers of AP courses, and supports universities. These activities are funded through fees charged to students taking AP Exams.</p>

<p>In 2006, over one million students took over two million Advanced Placement examinations. Many high schools in the United States offer AP courses, though the College Board allows any student to take any examination, regardless of participation in its respective course. Therefore, home-schooled students and students from schools that do not offer AP courses have an equal opportunity to take the examination.</p>

<p>As of the 2015 testing season, exams cost $91 each, though the cost may be subsidized by local or state programs. Financial aid is available for students who qualify for it; the exam reduction is $26 or $28 per exam from College Board plus an additional $8 rebate per fee-reduced exam from the school. There may be further reductions depending on the state. Out of the $89, $8 goes directly to the school to pay for the administration of the test, which some schools will reduce to lower the cost to the student.</p>

<p>On April 3, 2008, the College Board announced that four AP courses – French Literature, Latin Literature, Computer Science AB, and Italian Language and Culture – would be discontinued after the 2008–2009 school year due to lack of funding. However, the Italian Language and Culture test was again offered beginning in 2011.</p>

<p>Starting July 2013 AP allowed students for the first time to both view and send their scores online.</p>

<p>The number of AP exams administered each year has seen a steady increase over the past decade. In 2003, 175,860 English Language and Composition exams were administered. By 2013, this number had risen to 476,277, or an increase of 171%. Such an increase has occurred in nearly all AP exams offered, with the AP Psychology exam seeing a 281% increase over the past decade.</p>


