<p><b>Advanced Placement Biology (commonly abbreviated to AP Biology or AP Bio)</b> is a course and examination offered by the College Board to high school students as an opportunity to earn placement credit for a college-level biology course. For the 2012–2013 school year, the College Board unveiled a new curriculum with a greater focus on "scientific practices."</p>


<h3>Topics</h3>
<hr>

<p>Topics covered by this course include:</p>



<ul>
<li>Anatomy and Physiology</li>
<li>Biochemistry</li>
<li>Biodiversity</li>
<li>Cell functions and organelles</li>
<li>Developmental biology</li>
<li>Ecology</li>
<li>Genetics</li>
<li>Molecular biology</li>
<li>Origin of life</li>
<li>Population biology</li>
<li>Evolution</li>
<li>Molecular genetics</li>
<li>Cell Communication</li>
<li>Cell Division</li>
</ul>


<p>In addition to the standard biology topics above, students are required to be familiar with a set of 12 specific biology labs, as well as general lab procedure.</p>


<h3>Topic Makeup</h3>
<hr>

<p>The makeup of the AP Biology exam is based on the following percentages of three topics:</p>
<ul>
<li>Molecules and Cells, 25%</li>
<li>Heredity and Evolution, 25%</li>
<li>Organisms and Populations, 50%</li>
</ul>


<h3>The Exam</h3>
<hr>
<p>The AP test for this course consists of two sections. Section I, administered over a period of 90 minutes, consists of 63 multiple-choice questions and 6 mathematics-related fill in questions. Students are allowed to use a four-function calculator. Calculators are generally needed for this mathematics portion, and are allowed on the test day. Section II, which also lasts 90 minutes, consists of 2 essay prompts to be answered comprehensively by the student, as well as 6 shorter essays that require concise answers. A mandated 10 minute reading period is held prior to beginning to write the eight essays, leaving only 80 minutes for writing.</p>







<h3>Grade Distribution</h3>
<hr>

<p>In the 2007 administration, 144,796 students took the exam from 8,486 schools. In the 2008 administration, the exam grades were recalibrated, resulting in a substantial decrease in the top scores and increase in the bottom scores. In the 2009 and 2010 administrations, 159,580 and 172,512 students took the test, respectively. The 2013 revised test showed a marked decrease in the amount of 5's and 1's received.</p>

