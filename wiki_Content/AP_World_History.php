<p><b>Advanced Placement World History (also known as AP World History, WHAP, AP World, or APWH,)</b> is a college-level course that is offered to high school students through the College Board's Advanced Placement Program, designed to help students develop a greater understanding of the evolution of global processes and contacts as well as interactions between different types of human society. The course advances this understanding through a combination of selective factual knowledge and appropriate analytical skills. Students study all prehistory and history, especially from 8000 BCE to present-day.</p>

<h3>Course Structure</h3>
<hr>

<p>The course is organized around six eras/periods and nineteen "Key Concepts":</p>
<ul>
<li><b>Period 1</b> - Technological and Environmental Transformations, c. 8000 BCE to c. 600 BCE</li>
</ul>
<p>Key Concept 1.1 Big Geography and the Peopling of the Earth<br />
Key Concept 1.2 The Neolithic Revolution and Early Agricultural Societies<br />
Key Concept 1.3 The Development and Interactions of Early Agricultural, Pastoral, and Urban Societies</p>
<ul>
<li><b>Period 2</b> - Organization and Reorganization of Human Societies, c. 600 BCE to c. 600 CE</li>
</ul>
<p>Key Concept 2.1 The Development and Codification of Religious and Cultural Traditions<br />
Key Concept 2.2 The Development of States and Empires<br />
Key Concept 2.3 Emergence of Transregional Networks of Communication and Exchange</p>
<ul>
<li><b>Period 3</b> - Regional and Transregional Interactions, c. 600 CE to c. 1450 CE</li>
</ul>
<p>Key Concept 3.1 Expansion and Intensification of Communication and Exchange Networks<br />
Key Concept 3.2 Continuity and Innovation of State Forms and Their Interactions<br />
Key Concept 3.3 Increased Economic Productive Capacity and Its Consequences</p>
<ul>
<li><b>Period 4</b> - Global Interactions, c. 1450 CE to c. 1750 CE</li>
</ul>
<p>Key Concept 4.1 Globalizing Networks of Communication and Exchange<br />
Key Concept 4.2 New Forms of Social Organization and Modes of Production<br />
Key Concept 4.3 State Consolidation and Imperial Expansion</p>
<ul>
<li><b>Period 5</b> - c. 1750 CE to c. 1900 CE</li>
</ul>
<p>Key Concept 5.1 Industrialization and Global Capitalism<br />
Key Concept 5.2 Imperialism and Nation-State Formation<br />
Key Concept 5.3 Nationalism, Revolution, and Reform<br />
Key Concept 5.4 Global Migration</p>
<ul>
<li><b>Period 6</b> - c. 1900 CE to present</li>
</ul>
<p>Key Concept 6.1 Science and the Environment<br />
Key Concept 6.2 Global Conflicts and their Consequences<br />
Key Concept 6.3 New Conceptualizations of Global Economy, Society, &amp; Culture</p>



