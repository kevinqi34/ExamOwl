<p>The Multiple Choice section is weighted as 50% of one's total score.</p>
<p>While previously the exam deducted 1/4 of a point for every incorrect answer, starting from 2011 on, the penalty for incorrect answers had been removed. It is to one's advantage to attempt every question possible within the time limit. Note also that the number of multiple choice options is being reduced from five to four at the same time.</p>

<p>The second section, the free-response section, consists of 3 essays which may be answered in any order. However, it is recommended that students answer the DBQ first to get it out of the way as it requires little to no prior knowledge of historical background.</p>
<ul>
<li>(DBQ)</li>
<li>Continuity &amp; Change Over Time question (CCOT)</li>
<li>Compare and Contrast question (COMP)</li>
</ul>
<p>This section begins with a mandatory 10 minute reading period, during which students are instructed to read the DBQ, (including the documents) CCOT, and Comparative questions. It is vitally important to take careful notes and plan one's essays during these 10 minutes. After the reading period, students are given 120 minutes to write all 3 essays, though they may continue to take notes before starting to write their essays; notes taken during the reading period may be used. Each essay is out of 7 points (a detailed rubric can be found in the AP World History course description), but students can earn up to two points of extra credit after receiving 7 points by going above and beyond what is expected. Therefore, students can receive a maximum of 27 points on these essays. Students are encouraged to pace themselves.</p>

