<p><b>The Group 2: Language acquisition (previously Second Language)</b> subjects of the IB Diploma Programme consist of the nearly 80 additional languages offered and may be studied at the following levels: B (SL or HL), or ab initio (SL only). Latin and Classical Greek are also offered and may be taken at SL or HL. To earn an IB Diploma, a candidate must study an additional language, though a second Language A may be taken instead of studying that language as a Group 2 subject.</p>


<h3>Language ab initio SL</h3>
<hr>

<p>If a student has no previous background in a given language, then that student may study that language as an ab initio SL subject. Such a course focuses on giving the student basic knowledge of both the language in everyday use and the culture of the places where it is spoken. The standard reached by the student after two years is considerably lower than that reached in language B.</p>

<p><b>Curriculum</b></p>
<p>For students to achieve communicative competence in a variety of situations, the following core topics are explored in the Language ab initio course: the individual, education and work, town and services, food and drink, leisure and travel, the environment, health and emergencies. The language skills that are taught and assessed are: listening, reading, writing, speaking and cultural awareness.</p>

<p><b>Assessment</b></p>
<p>Internal Assessment — (30% of total grade) for a Language ab initio consists of the average of two oral presentations, one of which is to be given to the teacher, recorded and sent out for moderation. The other is an interactive oral activity and can be individual or group.</p>
<p>External Assessment — (70% of total grade) for Language ab initio includes paper 1, consisting of 3–4 reading comprehension texts and paper 2, consisting of one short writing task and one extended-response writing task.</p>
<p>For the oral and written components of the assessments, students receive marks based on their level of competency in use of language, presentation and communication skills.</p>
<p>For reading comprehension, students receive a grade based on a markscheme (answer key) for questions that are multiple-choice, short-answer, true/false/justify, matching and extended response.</p>

<p><b>Availability</b></p>

<p>Spanish ab initio and Mandarin ab initio are offered online to students enrolled in the IB Diploma Programme.</p>

<h3>Language B SL and HL</h3>
<hr>

<p>Language B is intended for students who have a previous background of 2–3 years in the language, and focuses more on learning to communicate in the language in written and spoken form.</p>
<p><b>Curriculum</b></p>
<p>Students study a variety of topics such as the environment, famous people, current and historical events, immigration, music, art, cuisine, fashion, film, etc.… to develop their skills in listening, reading, writing, speaking and cultural interaction. Types of texts studied include: news stories, short stories, brochures, advertisements, poems, informal and formal letters, excerpts from plays, editorials, debates, reviews and interviews.</p>

<p><b>Assessment</b></p>
<p>Internal Assessment — (30% of total grade) for a Language B consists of the average of two oral presentations, one of which is to be given to the teacher, recorded and sent out for moderation. The other is an interactive oral activity and can be individual or group.</p>
<p>External Assessment — (70% of total grade) for Language B includes paper 1, consisting of 3–4 reading comprehension texts and paper 2, consisting of a 250-word (SL) or 400-word (HL) written response, in the form of a journal entry, formal or informal letter, newspaper or magazine article or brochure.</p>
<p>For the oral and written components of the assessments, students receive marks based on their level of competency in use of language, cultural interaction and message.</p>
<p>For reading comprehension, students receive a grade based on a markscheme (answer key) for questions that are multiple-choice, short-answer, true/false/justify, matching and extended response.</p>

<h3>Language A2 SL and HL</h3>
<hr>
<p>The Language A2 is no longer offered by IB and integrated to Group 1 Language A. Language A2 corresponded to a near-native level of fluency in the foreign language, and as such concentrated less on studying the actual language and more on the literature and culture of the countries where the language is spoken.</p>
<p><b>Curriculum</b></p>
<p>The Languages A2 were studied through Cultural Options and Literary Options, both of which must be included by the teacher in the two-year IB course. Study at the A2 level was available in a significantly lower number of languages than at other levels. Many bilingual institutions supplemented their regular curriculum with an English A2 certification.</p>
<p><b>Assessment</b></p>
<p>Internal Assessment — for Language A2 consisted of two oral components, in the same way as for language A1.</p>
<p>External Assessment —for Language A2 consisted of paper 1, the Comparative Commentary, where the students write a commentary that compares the two previously unseen texts that appear in the exam. Paper 2 is an essay on either a Cultural or a Literary Option. In addition, students produced two Written Tasks of 1000–1500 words total to be assessed externally by the IB examiners.</p>


