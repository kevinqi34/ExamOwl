<p><b>Advanced Placement (AP) Physics 2</b>, along with AP Physics 1, is a year-long AP course designed by the College Board to replace AP Physics B in the 2014 - 2015 school year. The courses will teach the same general curriculum as AP Physics B, but instead splitting the course into two and cover more information, specifically rotational mechanics. The courses were formed through collaboration between current Advanced Placement teachers and The College Board, with the guidance of the National Research Council and the National Science Foundation. Similar to AP Physics C the course is said to cover the material of a first semester university undergraduate physics course offered at a typical American university, but with an algebra based curriculum as opposed to AP Physics C calculus based curriculum.</p>


<h3>History</h3>
<hr>
<p>The first AP Physics 2 classes will begin in the fall of the 2014-2015 school year, with the first AP exams administered in May 2015. As of August 2013 AP summer institutes, the College Board professional development course for Advanced Placement and Pre-AP teachers,[3] will dedicate 20% of the total to preparing AP Physics B educators for the new AP physics course. Face to face workshops sponsored by the College Board will begin to focus 20% their content on the new course as soon as September 2013. In February 2014 the official course description and sample curriculum resources will be posted to the College Board website, with 2 practice exams being posted the next month. As of September 2014 face to face workshops will be dedicated solely to AP Physics 1 & AP Physics 2. The full course will be taught starting in 2014, and the exam will be given 2015.</p>




<h3>Curriculum</h3>
<hr>

<p>AP Physics 2 is an algebra-based, introductory college-level physics course that explores topics such as fluid statics and dynamics; thermodynamics with kinetic theory; PV diagrams and probability; electrostatics; electrical circuits with capacitors; magnetic fields; electromagnetism; physical and geometric optics; and quantum, atomic, and nuclear physics. Through inquiry-based learning, students will develop scientific critical thinking and reasoning skills.</p>
<p>The College Board has released a "Curriculum Framework" which includes the 7 principles on which the new AP Physics courses will be based as well as smaller "Enduring Understanding" concepts.</p>



