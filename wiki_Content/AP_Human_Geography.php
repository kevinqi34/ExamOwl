<p><b>Advanced Placement Human Geography (also known as AP Human Geo, AP Geography, APHG, AP HuGe, HGAP, APHUG, AP HUMAN)</b> is an Advanced Placement social studies course that studies human geography. The test is administered by College Board.</p>

<p>This college-level course introduces students to the systematic study of patterns and processes that have shaped human understanding, use, and alteration of Earth's surface. Students employ spatial concepts and landscape analyses to analyze human social organization and its environmental consequences. They also learn about the methods and tools geographers use in their science and practice.</p>

<h3>Exam</h3>
<hr>

<p>The AP Human Geography Exam consists of two sections. The first section consists of 75 multiple choice questions and the second section consists of 3 free-response questions. The sections are 60 and 75 minutes long, respectively. It is not necessary to answer the free-response questions in essay form; instead, points are awarded on certain key words, examples, and other vital aspects.</p>
