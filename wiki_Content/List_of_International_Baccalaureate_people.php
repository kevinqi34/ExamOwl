<p>This is a list of notable people affiliated with the International Baccalaureate, including IB directors-general, chairs of the IB Board of Governors (previously known as the IB Council of Foundation), and notable graduates of one or more of the three IB programmes.</p>
<h3>IB directors-general</h3>
<hr>

<ul>
<li>Alec Peterson (1968–1977)</li>
<li>Gerard Renaud (1977–1983)</li>
<li>Roger Peel (1983–1998)</li>
<li>Derek Blackman (1998–1999)</li>
<li>George Walker (1999–2005)</li>
<li>Jeffrey Beard (2006-2013)</li>
<li>Dr Siva Kumari (2014 - present)</li>
</ul>

<h3>Chair of IB Board of Governors</h3>
<hr>

<ul>

<li>John Goormaghtigh (1968–1981)</li>
<li>Seydou Madani Sy (1981–1984)</li>
<li>Piet Gathier (1984–1990)</li>
<li>Thomas Hagoort (1990–1996)</li>
<li>Bengt Thelin (1996–1997)</li>
<li>Greg Crafter (1997–2003))</li>
<li>Monique Seefried (2003–2009)</li>
<li>Carol Bellamy (2009 - April 2015)</li>
<li>George Rupp (April 2015 - present)</li>

</ul>


<h3>Notable Alumni</h3>
<hr>

<ul>

<li>Princess Raiyah bint Al Hussein, daughter of King Hussein and Queen Noor of Jordan, graduate of the United World College of the Atlantic</li>
<li>Douglas Alexander, British Labour party Politician and current Shadow Secretary of State for Foreign Affairs, graduate of the Pearson College UWC in Canada</li>
<li>Willem-Alexander, Prince of Orange, King of the Netherlands, graduate of United World College of the Atlantic</li>
<li>Gael García Bernal, actor, graduate of the Edron Academy</li>
<li>Marina Catena, Director of the United Nations World Food Program, graduate of UWC Adriatic</li>
<li>Marina Diamandis, British musician and pop artist, who took the diploma in Greece</li>
<li>Anne Enright, 2007 Man Booker Prize award winning novelist, first Irish Laureate for Fiction and graduate of Pearson College UWC</li>
<li>Randa Abdel-Fattah, author, graduate of the Australian International Academy.</li>
<li>Alex Honnold, American professional rock climber, graduate of Mira Loma High School</li>
<li>Akihiko Hoshide, astronaut, graduate of the United World College of South East Asia</li>
<li>Ken Jennings, holds the record for the longest winning streak on the U.S. syndicated game show Jeopardy!, graduate of the Seoul Foreign School</li>
<li>Kim Jong-Chul, son of Kim Jong-Il, and brother of dictator of North Korea, attended International School of Berne, Switzerland</li>
<li>Sonam Kapoor, Bollywood actress, daughter of Anil Kapoor, graduate of the United World College of South East Asia</li>
<li>Robbie Kay, actor (Pirates of the Caribbean: On Stranger Tides, Once Upon a Time), graduate of the British School of Houston</li>
<li>Karen Mok, Hong Kong-based actress and singer-songwriter, attended UWC Adriatic</li>
<li>Mohamed Nasheed, President of the Maldives, attended Overseas Children's School (now Overseas School of Colombo), Sri Lanka</li>
<li>Lupita Nyong'o, Kenyan actress, (12 Years a Slave, Academy Award for best supporting actress 2014), graduate of St. Mary's School, Nairobi</li>
<li>Jorma Ollila, former CEO of Nokia, graduate of the United World College of the Atlantic</li>
<li>Ignacio Padilla, Mexican novelist and short story writer, graduate of Waterford Kamhlaba United World College of Southern Africa</li>
<li>Julie Payette, astronaut, graduate of the United World College of the Atlantic</li>
<li>Sarah Tan, Channel V VJ, graduate of the United World College of South East Asia</li>
<li>Toby Turner, YouTube star and actor on The High Fructose Adventures of Annoying Orange, graduated from Niceville High School in Florida</li>
<li>Abiodun Williams, President of The Hague Institute for Global Justice, graduate of Pearson College UWC</li>



</ul>

