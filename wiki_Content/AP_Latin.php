<p><b>Advanced Placement Latin (known also as AP Latin)</b>, formerly <b>Advanced Placement Latin: Vergil</b>, is an examination in Latin literature offered by the College Board's Advanced Placement Program. Prior to the 2012–2013 academic year, the course focused on poetry selections from the Aeneid, written by Augustan author Publius Vergilius Maro, also known as Vergil or Virgil. However, in the 2012–2013 year, the College Board changed the content of the course to include not only poetry, but also prose. The modified course consists of both selections from Vergil and selections from Commentaries on the Gallic War, written by prose author Gaius Julius Caesar. Also included in the new curriculum is an increased focus on sight reading. The student taking the exam will not necessarily have been exposed to the specific reading passage that appears on this portion of the exam. The College Board suggests that a curriculum include practice with sight reading. The exam is administered in May and is three hours long, consisting of a one-hour multiple-choice section and a two-hour free-response section.</p>

<h3>Abilities Tested</h3>
<hr>
<p>The exam tests students' abilities to:</p>
<ul>
<li>Translate a Latin passage from the syllabus into English literally</li>
<li>Explicate specific words and phrases in context</li>
<li>Identify the context and significance of short excerpts from the works specified in the syllabus</li>
<li>Identify and analyze characteristic or noteworthy features of the authors' modes of expression, including their use of imagery, figures of speech, sound effects, and metrical effects (in poetry only), as seen in specific passages</li>
<li>Discuss particular motifs or general themes not only suggested by passages but also relevant to other selections</li>
<li>Analyze and discuss structure and to demonstrate an awareness of the features used in the construction of a poem, thesis, or an argument</li>
<li>Scan the meters specified in the syllabus</li>
</ul>


<h3>Reading and Translation</h3>
<hr>
<p>Critical appreciation of the Aeneid as poetry implies the ability to translate literally, to analyze, to interpret, to read aloud with attention to pauses and phrasing, and to scan the dactylic hexameter verse. Students should be given extensive practice in reading at sight and in translating literally so that their translations not only are accurate and precise, but also make sense in English.</p>
<p>The instructions for the translation questions, "translate as literally as possible," call for a translation that is accurate and precise. In some cases an idiom may be translated in a way that makes sense in English but is rather loose compared to the Latin. In general, however, students are reminded that:</p>

<ul>
<li>The tense, voice, number, and mood of verbs need to be translated literally</li>
<li>Subject-verb agreement must be correct</li>
<li>Participles should be rendered precisely with regard to tense and voice</li>
<li>Ablative absolutes may be rendered literally or as subordinate clauses; however, the tense and number of the participle must be rendered accurately</li>
<li>Historical present is acceptable as long as it is used consistently throughout the passage.</li>
</ul>

<h3>Exam</h3>
<hr>
<p>The three hour exam consists of a one hour multiple-choice section and a two hour free-response section that includes fifteen minutes of reading time and one hour forty-five minutes of writing time. The multiple choice section includes approximately fifty questions that relate to four passages: three read at sight and one from the syllabus. The multiple choice questions test the many skills learned and practiced throughout the year, including:</p>
<ul>
<li>20–30% grammar and lexical questions (10–15 questions)</li>
<li>35–45% translation or interpretation of a phrase or sentence (17–23 questions)</li>
<li>2–5% metrics: that is, scansion of the dactylic hexameter line (1–3 questions)</li>
<li>2–5% figures of speech (1–3 questions)</li>
<li>20–30% identification of allusions or references, recognition of words understood but unexpressed, explication of inferences to be drawn (10–15 questions)</li>
<li>2–5% background questions on the <i>Aeneid</i> passage only (1–3 questions)</li>
</ul>
<p>The free-response section includes translation, analysis, and interpretation of the Latin text from the syllabus. The format is as follows:</p>
<ul>
<li>Question 1: a 10-minute translation</li>
<li>Question 2: a 10-minute translation</li>
<li>Question 3: a 45-minute long essay</li>
<li>Question 4: a 20-minute short essay</li>
<li>Question 5: a 20-minute short essay based on the entire <i>Aeneid</i> (Latin selections and parts read in English)</li>
</ul>



<h3>Grade Distribution</h3>
<hr>
<p>In the 2010 administration, 6,523 students took the exam, and 4,114 passed (3 or higher), or about 63.1%. In the 2011 administration, 6,044 students took the exam, and 3,861 passed (3 or higher), or about 63.9%. In the 2012 administration, 18,161 students took the exam, and 11,244 passed (3 or higher), or about 61.9%.</p>
<p>In the 2013 administration of the redesigned exam, 6,667 students took the exam, and 4,442 passed (3 or higher), or about 66.6%. In the 2014 administration of the exam, 6,542 students took the exam, a slight decrease from last year, and 4,307 passed (3 or higher), or about 65.8%, a slight decrease from last year's pass rate.</p>
