<p><b>Advanced Placement Latin Literature (also AP Latin Lit)</b> was one of two examinations (the other being AP Latin) offered by the College Board's Advanced Placement Program for high school students to earn college credit for a college-level course in Latin literature.</p>
<p>Due to low numbers of students taking AP Latin Literature, it was discontinued after the 2008–09 year. The AP Latin exam is now the sole Latin exam offered by the College Board.</p>

<h3>Material Tested</h3>
<hr>
<p>The AP Latin Literature exam was designed to test students' knowledge of a range of classical Latin literature, as opposed to the AP Latin: Vergil examination, which focuses almost exclusively on Virgil's Aeneid. The course itself followed one of three different curricula, each focusing on a different pair of authors: Catullus-Cicero, Catullus-Horace, and Catullus-Ovid. For each syllabus, students were expected to be able to read, translate, interpret, and analyze selected poems by Catullus as well as selected writings of the secondary author.</p>
<p>All students were expected to be familiar with the following poems (approximately 800 lines worth) by Catullus: 1-5, 7-8, 10-13, 14a, 22, 30-31, 35-36, 40, 43-46, 49-51, 60, 64 (lines 50-253), 65, 68 (lines 1-40), 69-70, 72, 76-77, 84-87, 96, 101, 109, and 116.</p>
<p>Students who chose to study Cicero as their secondary author read the entire contents of Pro Archia Poeta Oratio and Sections 17-23 and 100-104 from De Amicitia. It was also suggested that students read the entire English translation of De Amicitia.</p>
<p>Students who chose to study Horace read the following Odes: 1, 5, 9, 11, 13, 22-25, and 37-38 from Book 1; 3, 7, 10, and 14 from Book 2; 1, 9, 13, and 30 from Book 3; and 7 from Book 4. They also read 1.9 from his Sermones.</p>
<p>Students who chose to study Ovid read the following stories from his Metamorphoses: Apollo and Daphne (lines 452-567 from Book 1), Pyramus and Thisbe (lines 55-166 from Book 4), Daedalus and Icarus (lines 183-235 from Book 8), Baucis and Philemon (lines 616-724 from Book 8), and Pygmalion (lines 283-297 from Book 10). They must also read 1.1, 1.3, 1.9, 1.11, 1.12, and 3.15 from his Amores.</p>

<h3>Abilities Tested</h3>
<hr>
<p>The AP Latin Literature exam tested students' abilities to:</p>

<ul>
<li>Translate literally a selected Latin passage</li>
<li>Explicate certain words or phrases in context</li>
<li>Identify the content and significance of selected excerpts</li>
<li>Identify and analyze characteristic or noteworthy features of the authors' writing, including use of imagery, figures of speech, metrical and sound effects</li>
<li>Discuss particular themes or motifs, not only those suggested by passages, but also those relevant to other sections</li>
<li>Analyze and discuss structure, as well as demonstrate awareness of the features used in the construction of a poem or argument</li>
<li>Scan the meter of selections</li>



</ul>


<h3>Exam Format</h3>
<hr>

<p>The AP Latin Literature exam began with a 60-minute multiple-choice section. Students were given four passages, three of which were shared with the AP Latin: Vergil exam and one of which was a passage of Catullus that students should have already studied. The multiple-choice questions were concerned with comprehension, translation, metrical scanning, poetic devices, and grammatical structures.</p>
<p>The students were then given a 120-minute free-response section. This section began with a 15-minute reading period, during which students would view the free-response questions and outline their responses, but could not begin writing the essays. Students then had the remaining 105 minutes to respond to the questions.</p>
<p>Twelve questions were provided, three for each author on the syllabus. All students would have to answer the questions pertaining to Catullus. They then answered the three questions pertaining to the secondary author that they studied. It was suggested that students devote one hour of the essay-writing period to the Catullus questions and forty-five minutes to the remaining three questions.</p>
<p>Each set of questions included one short translation of a poem or passage from the syllabus. For each of the remaining two Catullus questions, students were provided with one or two poems or excerpts of poems and must respond to a specific questions concerning the poetry. They must have also answered a similar prompt for one of the essays for their secondary author. For the final question of the free-response section, students were given a passage from the secondary author. They must have responded to short-answer questions concerning poetic devices, grammatical structures, and comprehension of both the literal text and implications from the grammar and vocabulary.</p>


