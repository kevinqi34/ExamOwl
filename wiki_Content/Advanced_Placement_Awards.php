<p>The College Board offers several awards to select students who take Advanced Placement (AP) exams.</p>


<h3>AP Scholar Designations</h3>
<hr>
<p>Each year, the AP program recognizes students who have performed exceptionally well on AP examinations. Exams are taken in May and awards are usually granted in September. The following designations can be earned:</p>




<div class="tableoverflow">

<table>
<caption>AP Scholar Awards</caption>
<tr>
<th>Designation</th>
<th>Criteria</th>
</tr>
<tr>
<td>AP Scholar</td>
<td>Grades of <b>3</b> or better on <b>three</b> or more AP exams.</td>
</tr>
<tr>
<td>AP Scholar with Honor</td>
<td>Grades of <b>3</b> or better on <b>four</b> or more AP exams <b>and</b> an average of <b>3.25</b> on all AP exams taken.</td>
</tr>
<tr>
<td>AP Scholar with Distinction</td>
<td>Grades of <b>3</b> or better on <b>five</b> or more AP exams <b>and</b> an average of <b>3.5</b> on all AP exams taken.</td>
</tr>
<tr>
<td>National AP Scholar</td>
<td>Grades of <b>4</b> or better on <b>eight</b> or more AP exams <b>and</b> an average of <b>4</b> on all AP exams. Must be a student in the United States.</td>
</tr>
<tr>
<td>National AP Scholar (Canada)</td>
<td>Grades of <b>4</b> or better on <b>five</b> or more AP exams <b>and</b> an average of <b>4</b> on all AP exams taken. Must be a student in Canada.</td>
</tr>
<tr>
<td>National AP Scholar (Bermuda)</td>
<td>Grades of <b>4</b> or better on <b>five</b> or more AP exams <b>and</b> an average of <b>4</b> on all AP exams taken. Must be a student in Bermuda.</td>
</tr>
<tr>
<td>State AP Scholar</td>
<td>Top male and female student in each U.S. state (and the District of Columbia) ranked first by the greatest number of exams with a grade <b>3</b> or higher and then by highest average on all* AP exams taken.</td>
</tr>
<tr>
<td>DoDEA AP Scholar</td>
<td>Top male and female student among all DoDEA (Department of Defense Education Activity) schools, ranked first by the greatest number of exams with a grade <b>3</b> or higher and then by highest average on all* AP exams taken. Any recipient of this award must also at least meet the criteria for an AP Scholar.</td>
</tr>
<tr>
<td>International AP Scholar</td>
<td>Same as the AP Scholar Award except the student must attend a school outside the U.S. and Canada (which must also not be a DoDEA school).</td>
</tr>
</table>

</div>

<p>Note: "All AP exams taken" refers to all AP exams taken in any year. It is not restricted to the year in which the award is issued.</p>


<h3>AP International Diploma</h3>
<hr>


<p>The AP program also awards the AP International Diploma for Overseas Study (APID) to students who have applied to colleges outside of the United States that have completed a sequence of AP exams with satisfactory grades. Prior to May 2006, a student had to earn a grade of 3 or better on five or more AP exams in three of the five subject areas shown in the table below, with certain subject area requirements. As of May 2006, the College Board had implemented new requirements for awarding the AP International Diploma.</p>


