<p><b>Advanced Placement European History (commonly known as AP Modern European History, AP Euro, AP Eurhist, AP European, EHAP or APEH)</b>, is a course and examination offered by the College Board through the Advanced Placement Program. This course is for high school students who are interested in a first year university level course in European history. The course surveys European history from the year 1450 to 2001, focusing on religious, social, economic, and political themes.</p>

<h3>The Exam</h3>
<hr>
<p>The AP exam for European History is composed of two sections comprising 80 multiple-choice questions and 3 essay responses. Section I, the multiple-choice portion, is to be completed in 55 minutes. Section II, the written response portion, is to be completed in 130 minutes (including the mandated 15-minute reading period) and includes three essays (two of them thematic Free Response Questions (FRQs) and one Document Based Question (DBQ)). The DBQ is provided a mandated 15-minute reading period prior to beginning the response. Each essay is graded on a scale of nine possible points.</p>
<p>The exam grade is weighted evenly between the multiple-choice and free-response sections. The DBQ is weighted 45 percent of the Free Response Question score, leaving the thematic essays weighted 55 percent, 27.5 percent each.</p>
<p>Approximately half of the multiple-choice questions cover the period from 1450 to the French Revolution and Napoleonic era, and half cover the period from the French Revolution and Napoleonic era to the present, evenly divided between the nineteenth and twentieth centuries. About one-third of the questions focus on cultural and intellectual themes, one-third on political and diplomatic themes, and one-third on social and economic themes. Many questions draw on knowledge of more than one chronological period or theme. Although this is the general trend based on past AP Exams, it is not mandated that the exam follow this format.</p>




