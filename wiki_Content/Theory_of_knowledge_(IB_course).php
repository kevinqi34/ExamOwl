<p><b>TOK essay</b></p>

<p>For each exam session the IB prescribes 6 essay titles from which students must choose, e.g. "All knowledge claims should be open to rational criticism. On what grounds and to what extent would you agree with this assertion?" Each title raises generic cross-disciplinary questions about knowledge, and the student is expected to consider the issues raised in the title and reach conclusions about them. The essay should put forward claims and counterclaims, linking knowledge issues to areas of knowledge and ways of knowing, and show evidence of original thinking by the student. Essays over the maximum word count of 1,600 are penalised with a one mark reduction, and any content beyond the 1600th word of the essay is not read by the examiner.</p>

<p><b>TOK presentation</b></p>

<p>During the Theory of Knowledge course students must plan and deliver at least one (in individual or small group, maximum three students) presentation to the class. The topic should be based on a real-life situation of interest to the student, e.g. "Reliability of media reporting of science", "What makes something a work of art?" and the presentation is expected to show why the topic is significant, linking it to relevant knowledge issues, discussing those issues and examining the implications of approaching the question from different perspectives. Teachers have wide latitude to help with topic selection and identifying suitable approaches. About ten minutes should be allowed for each presenter, and almost any form is permitted (e.g. debates, games, skits, interviews etc.) except reading an essay aloud.</p>

