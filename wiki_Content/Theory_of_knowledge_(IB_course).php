<p><b>Theory of knowledge</b> is a compulsory subject in the International Baccalaureate Diploma Programme which is, in essence, similar to epistemology courses offered at many universities.</p>

<h3>Course description</h3>
<hr>

<p>Unlike standard academic disciplines, the Theory of Knowledge course uses a process of discovering and sharing students' views on "knowledge questions" (an umbrella term for "everything that can be approached from a TOK point of view"), so "there is no end to the valid questions that may arise", "there are many different ways to approach TOK," "the sheer scope of the TOK course is daunting" and "teachers and students need the confidence to go too far outside their traditional comfort zones." Teachers have freedom to select a teaching methodology and course material that will convey the theoretical foundation of essential concepts, and may provide an environment in which these concepts can be discussed and debated. The focus of the discussion should not be the differentiation between "right" and "wrong" ideas, but on the quality of justification and a balanced approach to the knowledge claim in question.</p>
<p>The TOK course uses a combination, in no particular order ("many entry points and sequences are possible"):</p>

<ul>

<li>Ways of knowing: (sense perception, reason, emotion, faith, imagination, intuition, memory, and language). How do we gain knowledge of the world, and what are the advantages and disadvantages of each way in which we learn of the world and our place in it. Until the fall of 2014, there were only four ways of knowing (sense perception, reason, emotion, and language, but the IB curriculum then changed to include four other ways of knowing: intuition, imagination, faith, and memory.</li>
<li>Areas of knowledge (mathematics, natural sciences, human sciences, history, religious knowledge systems, indigenous knowledge systems, the arts and ethics): their distinct natures and methods of gaining knowledge, the types of claim each makes and the issues to consider (e.g. "How do you know that the scientific method is a valid method of gaining knowledge?", "What is the reason for having historical knowledge, and how is it applied in life?"). The IB originally had six areas of knowledge: mathematics, natural sciences, human sciences, history, the arts and ethics. In the fall of 2014, the IB curriculum changed to include two more areas of knowledge: religious knowledge systems and indigenous knowledge systems.</li>
<li>Factors that transcend individual ways of knowing and areas of knowledge:</li>

<ul>
<li>Nature of knowing: what are the differences between information, data, belief, faith, opinion, knowledge and wisdom?</li>
<li>Knowledge communities: what is taken for granted in a community? How can we decide which beliefs we ought to check further?</li>
<li>Knowers' perspective and applications of knowledge: how do age, education, culture and experience influence selection of sources and formation of knowledge claims? If you know something, or how to do something, do you have a responsibility to use your knowledge?</li>
<li>Justifications of knowledge claims: why should claims be assessed critically? Are logic, sensory perception, revelation, faith, memory, consensus, intuition, and self-awareness equally reliable justifications? Use of coherence, correspondence, pragmatism, and consensus as criteria of truth.</li>

</ul>

</ul>

<p>The TOK course is expected to involve 100 teaching hours over the two years of the Diploma Programme. Having followed the course, students should be competent to analyse knowledge claims and respond to knowledge issues in the context of different areas of knowledge and ways of knowing, expressing ideas accurately and honestly, using examples from their own experiences as learners and in outside life.</p>

<h3>Assessment</h3>
<hr>

<p>Theory of knowledge is assessed in two parts: an externally examined 1,200–1,600 word essay and an internally assessed presentation. Each part is scored using assessment criteria (four criteria for the essay and four for the presentation) that describe levels of achievement (e.g. "The inquiry explores knowledge issues. Most points are justified; most arguments are coherent. Some counterclaims are considered." describes level 5–6 in one of the essay criteria). The total score is converted into a grade from A to E. A similar system is used for the extended essay and students can gain up to 3 points for the diploma based on the grades achieved for TOK and EE. No diploma is awarded if a candidate fails to submit both a TOK essay and TOK presentation, or receives grade E for both the extended essay and theory of knowledge.</p>

