<p><b>Advanced Placement German Language (also known as AP German Language or AP German)</b> is a course and examination provided by the College Board through the Advanced Placement Program. This course is designed to give high school students the opportunity to receive credit in a college-level German language course.</p>

<p>Originally the College Board had offered two AP German exams, one with AP German Language and another with AP German Literature. In 1983, however, due to the persistently low number of students taking the AP German Literature exam, it was dropped from the AP Portfolio and since then only AP German Language has been offered.</p>

