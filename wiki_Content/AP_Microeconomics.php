<p><b>Advanced Placement Microeconomics (AP Microeconomics)</b> is a course for students interested in college-level work in microeconomics and/or gaining advanced standing in college. The course begins with a study of fundamental economic concepts such as scarcity, opportunity costs, production possibilities, specialization, and comparative advantage. Major topics include the nature of functions of product markets; factor markets; and efficiency, equity, and the role of government.</p>


<h3>Topic Outline</h3>
<hr>
			<ul>
				<li>Basic Economic Concepts (8-14%)</li>
				<li>The Nature and Functions of Product Markets (55-70%)</li>
				<li>Factor Markets (10-18%)</li>
				<li>Market Failure and the Role of Government (12-18%)</li>
			</ul>


<h3>The Exam</h3>
<hr>
<ul>
<li><b>Multiple Choice</b> [70 minutes, weighted 2/3 (66.7%) of the total exam score]</li>
<li><b>Free-Response</b> [10 minutes of planning then 50 minutes of writing with one longer free-response question and two shorter ones, weighted 1/3 (33.3%) of the total exam score]</li>
</ul>


