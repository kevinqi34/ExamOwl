<p><b>AP Capstone</b> is a new pilot program developed by the College Board. It consists of a two-year high school program and two new courses: the AP Seminar and the AP Research courses. Students who successfully complete the program and obtain scores of 3 or higher on at least four other AP exams receive either an AP Capstone Diploma or an AP Capstone Certificate.</p>

<h3>History</h3>
<hr>
<p>The pilot predecessor to the current AP Capstone program, the AP|Cambridge Capstone Program, was initially offered in 17 high schools worldwide. With the launch of the full AP Capstone by the College Board in fall 2014, the program has expanded to now being offered in 136 schools, including Osbourn Park High School, as well as Felix Varela Senior High. Over 100 colleges and universities support the AP Capstone program.</p>

<h3>AP Seminar</h3>
<hr>
<p>The AP Seminar is a foundation course typically taken in Grade 11, though it may also be taken by students in Grade 10. The AP Seminar provides students with the opportunity to develop critical thinking skills and prepare for university. It is based on three main components: a team project and presentation, a research-based essay and presentation, and a written exam. Students explore real-world issues such as innovation, sustainability and technology. Students must receive a score of three or higher in the course to continue. The final exam for this course consists of three components, a group research paper and presentation, an individual research paper and presentation, and a final writing exam.</p>

<h3>AP Research</h3>
<hr>
<p>The AP Research course is typically taken in Grade 12. Students design, plan and conduct a year-long research-based investigation on a personally-chosen subject. The assessment culminates with a 5000-word academic thesis paper, as well as a public presentation. Students must obtain a final score of three or higher to be able to receive AP certification.</p>

<h3>Completion of Program</h3>
<hr>
<p>In addition to completing the AP Seminar and AP Research courses, students also take at least four other AP courses of their choice. Upon receiving at least a score of three on all four exams and getting at least a score of three on the AP Seminar and AP Research courses, they will receive an AP Capstone Diploma upon graduation. If students do not score three or higher on all exams, they will receive the AP Capstone Certificate.</p>


<h3>College Recognition</h3>
<p>The AP Capstone program's recent implementation has left it often unrecognized by a multitude of colleges and universities. The program looks to expand further and reach out to the colleges and universities to expand the recognition of the program for college-bound students.</p>


