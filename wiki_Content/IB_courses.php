<p>The <b>International Baccalaureate (IB)</b>, formerly known as The International Baccalaureate Organization (IBO), is an international educational foundation headquartered in Geneva, Switzerland, founded in 1968. IB offers four educational programmes for children aged 3–19. The organization's name and logo were changed in 2007 to reflect a reorganisation. Consequently, "IB" can refer to the organisation itself, any of the four programmes, or the diploma or certificates awarded at the end of the programme.</p>

<p>Marie-Thérèse Maurette created the framework for what would eventually become the IB Diploma Programme (IBDP) in 1948 when she wrote 'Educational Techniques for peace. Do they exist?'. n the mid-1960s, a group of teachers from the International School of Geneva (Ecolint) created the International Schools Examinations Syndicate (ISES), which would later become the International Baccalaureate (IB). by Peter Nehr, International Baccalaureate Africa, Europe and Middle-East (IBAEM) was established in 1986, and International Baccalaureate Asia Pacific (IBAP) established during the same period.</p>

<p>The IB Middle Years Programme (MYP) adheres to the study of eight subject areas and was developed and piloted in the mid-1990s. Within five years 51 countries had MYP schools. The IB Primary Years Programme (PYP) was piloted in 1996 in thirty primary schools on different continents, and the first PYP school was authorised in 1997, with as many as 87 authorised schools in 43 countries within five years. The newest offering from the IB, the IB Career-related Programme (formerly IB Career-related Certificate) is designed for students of ages 16 to 19 who want to engage in career-related learning. The IB introduced its newly reviewed MYP for first teaching in September 2014.</p>


<h3>The IB Learner Profile</h3>
<hr>

<p>The IB Learner Profile is as follows:</p>

<ul>
<li>Inquirers</li>
<li>Knowledgeable</li>
<li>Communicators</li>
<li>Principled</li>
<li>Open-minded</li>
<li>Caring</li>
<li>Risk-takers</li>
<li>Balanced</li>
<li>Reflective</li>
</ul>

<p>All four programmes (PYP, MYP, DP and CP) use the IB learner profile.</p>


