<h3>Organization</h3>
<hr>

<p>The IB is a not-for-profit educational foundation. The IB maintains its Foundation Office in Geneva, Switzerland. The Assessment Centre is located in Cardiff, Wales and the curriculum centre moved in 2011 to The Hague, Netherlands. Three Global Centres have been opened: Bethesda Maryland, United States, Singapore and The Hague, Netherlands.</p>
<p>The organisation is divided into three regional centres: IB Africa, Europe and Middle East (IBAEM), administered from The Hague; IB Americas (IBA), administered from Bethesda and Buenos Aires, Argentina; and IB Asia-Pacific (IBAP), administered from Singapore.</p>
<p>Sub-regional associations "are groups formed by and for IB school practitioners to assist IB schools, teachers and students in their communities—from implementing IB programmes to providing a forum for dialogue." here are currently fifty-six (56) sub-regional associations, including:</p>
<ul>
<li>fifteen (15) in the IB Africa, Europe and Middle East (IBAEM) region</li>
<li>thirty-six (36) in the IB Americas (IBA) region and</li>
<li>five in the IB Asia Pacific (IBAP) region.</li>
</ul>

<p>In 2003, the IB established the IB Fund, incorporated in the United States, for the purpose of enhancing fundraising and keeping funds raised separate from operational funds. In 2004, the IB approved a strategic plan to "ensure that programmes and services are of the highest quality" and "to provide access to people who are socio-economically disadvantaged." In 2010 the strategic plan was updated after substantial consultation. The vision for the next 5 years was to more consciously establish the IB as a leader in international education and the Board outlined a vision and four strategic goals with key strategic objective.</p>
<p>Access remains fundamental to the mission of the IB and a variety of initiatives and projects are helping to take it forward in Ecuador, Poland, Romania, Czech republic, South Africa, Kazakhstan, Spain, Malaysia, and Japan</p>

<p>The United States has the largest number of IB programmes (2,010 out of 5,586) offered in both private and public schools.</p>
<p>The IB has consultative status as a non-governmental organisation (NGO) at United Nations Educational, Scientific and Cultural Organization (UNESCO) and has collaborative relationships with the Council of Europe and the Organisation Internationale de la Francophonie (OIF).</p>

<h3>Governance</h3>
<hr>
<p>The IB governance is composed of an IB Board of Governors and six committees (access and advancement, audit, education, finance, human resources and governance). The Board of Governors appoints the Director general, sets the strategic direction of the organisation, adopts a mission statement, makes policy, oversees the IB's financial management, and ensures autonomy and integrity of the IB Diploma Programme examinations and other student assessment. The structure of its different committees are based on respect, representation and collaboration.</p>
<p>The Board of Governors can comprise between 15 and 25 members. Members are elected by the Board on the recommendation of the governance committee, and from nominations presented from the Heads Council, Regional Councils and the Board. To encourage diversity of gender, culture and geography, there are only three ex officio positions: Director general (non-voting), the chair of the Examining Board and the chair of the Heads Council.</p>
<p>Advisory bodies include the Heads Council and Regional Councils</p>

<h3>Reception</h3>
<hr>

<p>The IB Diploma Programme was described as "a rigorous, off-the-shelf curriculum recognized by universities around the world" when it was featured in the December 18, 2006, edition of Time titled "How to bring our schools out of the 20th Century". The IBDP was also featured in the summer 2002 edition of American Educator, where Robert Rothman described it as "a good example of an effective, instructionally sound, exam-based system." n 2006, as part of the American Competitiveness Initiative (ACI), President George W. Bush and Education Secretary Margaret Spellings presented a plan for the expansion of Advanced Placement (AP) and International Baccalaureate mathematics and science courses, with the goal of increasing the number of AP and IB teachers and the number of students taking AP and IB exams, as well as tripling the number of students passing those exams.  Howard Gardner, a professor of educational psychology at Harvard University, said that the IBDP curriculum is "less parochial than most American efforts" and helps students "think critically, synthesize knowledge, reflect on their own thought processes and get their feet wet in interdisciplinary thinking."</p>
<p>In 2006, government ministers in the United Kingdom provided funding so that "every local authority in England could have at least one centre offering sixth-formers the chance to do the IB." In 2008, due to the devaluing of the A-Levels and an increase in the number of students taking the IB exams, then-Children's Secretary Ed Balls abandoned a "flagship Tony Blair pledge to allow children in all areas to study IB." Fears of a "two-tier" education system further dividing education between the rich and the poor emerged as the growth in IB is driven by private schools and sixth-form colleges.</p>
<p>In a 2006 attempt to eliminate it from a public school in Pittsburgh, PA. Some schools in the United States have eliminated the IBDP due to budgetary reasons and low student participation. In Utah in 2008, funding for the IBDP was reduced from $300,000 to $100,000 after State Senator Margaret Dayton objected to the program, stating, "First, I have never espoused eliminating IB ...I don't want to create 'world citizens' nearly as much as I want to help cultivate American citizens who function well in the world." But not everyone agrees and Mayor Rahm Emanuel of Chicago believes IB needs to be an option for students in Chicago Public Schools. Elizabeth Brackett reports on the IB in Chicago. A report by the University of Chicago concluded that Chicago Public School students who completed the IB programme were 40% more likely to attend a four-year college, 50% more likely to attend a selective four-year college, and significantly more likely to persist in college than their matched peers outside the program.” The City of Miami Beach Commission entered into an education compact with Miami-Dade County Public Schools with one of the initiatives of the compact to implement the IB program throughout Miami Beach feeder schools.</p>
<p>In other parts of the world IB programs has been well received. In 2013, The Ministry of Education, Culture, Sports, Science and Technology of Japan and the IB announced a plan that will expand the opportunities for Japanese students to complete the IB curriculum in Japanese. In Malaysia a project has been developed in response to interest expressed by the Malaysia Ministry of Education (MoE) in working with the IB to implement the IB Middle Years Programme (MYP) in select secondary state schools. The Abu Dhabi Education Council (ADEC) signed an agreement with the IB in efforts to widen the options offered for parents and to meet the different needs of students in the United Arab Emirates (UAE). In April 2014 The King Faisal Foundation in Saudi Arabia and the IB signed a memorandum of understanding to develop IB programs, including the IBDP, in up to 40 primary and secondary schools, with the goal of developing these schools as centres of excellence as IB World Schools. Internationally the IB continues to be recognised as innovative. In 2014 The World Innovation Summit for Education (WISE) announced the IB Career-related Certificate as a finalist for their annual WISE Awards.</p>


