<li>Language Acquisition</li>
<li>Individuals and Societies</li>
<li>Sciences</li>
<li>Mathematics</li>
<li>The Arts</li>
</ul>


<h3>Career-related Programme (CP) curriculum outline</h3>
<hr>
<p>Age Range: 16-19</p>
<p>Three-part framework</p>
<ul>
<li>Study of at least two Diploma Programme courses</li>
<li>Career-related studies (terminology differs across the world - vocational, professional, technical qualifications and other definitions).</li>
<li>CP core</li>
</ul>
<p>CP Core</p>
<ul>
<li>Approaches to learning (ATL) course</li>
<li>Community and service</li>
<li>Reflective project</li>
<li>Language development</li>
</ul>


