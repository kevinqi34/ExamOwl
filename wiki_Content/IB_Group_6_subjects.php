<p><b>The Group 6: The Arts</b> subjects of the International Baccalaureate Diploma Programme consist of five courses at both Standard Level (SL) and Higher Level (HL): Dance, Music, Theatre, Visual Arts, and Film. The transdisciplinary course Literature and Performance (satisfying the requirements of Groups 1 and 6) is also available at Standard Level. Students seeking the IB Diploma may substitute courses from the other five Subject Groups instead of taking a Group 6 course (see below). A school-based syllabus devised by an IB World School, as approved and externally moderated by the IB, may also form the basis for a course taken in place of a Group 6 course.</p>

<h3>Music SL and HL</h3>
<hr>

<p>IB Music can be taken at either the standard level (SL) or higher level (HL). For both levels of IB Music the candidate must conduct a musical investigation. This requires research of two completely different musical genres with comparable qualities (e.g. Tuvan throat singing and Baroque opera). The information will then be presented in the form of a media script which will be assessed externally.</p>
<p>There are three paths in which the IB Music SL course can be taken: group performance, solo performance, or composition. To fulfill the performance requirements, the candidate must perform either a 15 minute solo recital, or a 20-30 minute ensemble performance. A candidate who chooses composition should compose two original compositions (of which one may be an arrangement of an existing piece of music), each lasting between 3 and 6 minutes. Each of the two compositions should be recorded for assessment purposes. A score of both compositions must also be submitted.</p>

<p>The IB Music HL course combines a medley of the IB Music SL options into a single curriculum. To satisfy IB Music HL, the candidate must perform solo for a total of 20 minutes and write three contrasting compositions (of which one may be an arrangement), each between 3 and 6 minutes.</p>
<p>All candidates sit a written exam at the end of the course, which lasts 3 hours for HL candidates and 2 hours 15 minutes for SL candidates. HL and SL candidates answer two questions from section A (study of two prescribed works), of which one MUST be a comparison between the two prescribed works. HL and SL candidates are also required to complete Section B of the exam, which is aural analysis of four previously unheard extracts, of which two are named, and one has a score provided. Two candidates are unnamed and do not have a score provided. HL only candidates must also complete section C, a comparison of two extracts from section B. Section A responses must be written in essay form. Responses in sections B and C may be in bullet points.</p>

<p>Further details of the course can be found in the IB Music Wikibook.</p>

<h3>Theater SL and HL</h3>
<hr>

<p>According to the new syllabus introduced in 2009, the IB Theater Arts programme is made out of four components; two are internally assessed and two are externally assessed. The internal assessments are the Theater Performance and Production Presentation (TPPP) and the Independent Project Portfolio (IPP). The external components are a Practical Performance Proposal (PPP) and a Research Investigation (RI).</p>
<p>The TPPP is a presentation on the student's involvement in their performance and production aspects of all areas of the core syllabus. The presentation is 30 minutes long for HL and should be supported by 7–10 visual materials (no larger than A4). For SL, the presentation is 20 minutes long and should be supported by 5–7 visual materials (no larger than A4).</p>
<p>The IPP is a portfolio of 3000 words at HL and 2000 at SL in which the student reflects on his/her learning and development during the production of an independent project, which is a project in which the student explores and practices a role in the theater (actor, director, dramaturg, scriptwriter, etc.). It should also show a connection to their experiences in the core syllabus. There are two options: Option A: Devising Practice and Option B: Exploring Practice. The portfolio must include sections marked Preparation, Action, and Reflection.</p>
<p>For the PPP, the student has to adopt a directorial perspective and write a concept for a play using one of the prescribed stimuli. For SL, it contains a 250-word pitch and explanatory, visual material that illustrates the student's understanding of the intended process of realization. For HL, it also includes a 1,000-1,250 word rationale.</p>
<p>The RI is a research essay in which the student presents his or her research on a previously unfamiliar theater practice. From the chosen theater practice, students should choose a specific aspect of a play or theater piece and create a research question to answer. Student's research should contribute to a realization of the play or theater piece from their chosen theatrical practice. The practice cannot be studied in class and must be no later than the 19th century. The question must be taken from a directorial, actor, or designer perspective. For SL, the essay must be 1,500-1,750 words with visual documentation and/or textual references. For HL, the essay must be 2,000-2,500 words with visual documentation and/or textual references. At HL, students must also write a critique of the sources in the research investigation.</p>

<h3>Visual Arts SL and HL</h3>
<hr>

<p>There are two areas of focus in the IB Visual Arts subject. The first is studio (practical work) and the second is the research workbook (known as the Investigation Workbook). The Visual Art program aims to teach the student about design, structure and the aesthetic development of work. The candidates must demonstrate creative and personal thinking, feeling and interaction with their work.</p>

<p>The exam for Visual Art encourages the candidate to articulate their concerns and development over the course of the two years of study. An exhibition will be held at the candidate's school showcasing the candidate's work and is assessed by his/her teacher. The assessment is an interview where the candidate talks about their exhibited works, this interview is recorded and sent overseas for moderation. However, the alternate to the interview process is to write a 1000 word statement on your IB journey in Visual Arts. This given mark is then moderated against the Record of Workbook, which contains a collection of photographs of the candidates work and a number of scanned pages from their research workbook.</p>

<p>The candidates' research workbooks are also marked, once internally and once externally (for moderation purposes). These books aim to show the candidates journey over the two years of study. They document art and design history that is relevant to the candidate’s exploration of ideas and will also contain notes, sketches, photographs, mind-maps and pictures of inspiration, development and final works. The candidate must also document a number of art exhibition visits.</p>

<p>For the final assessment, either the studio work or the investigation work book can be assessed externally. With 60% of the final grade being placed on that which is externally assessed, and the remaining 40% on the internal assessment.</p>

<h3>Film SL and HL</h3>
<hr>
<p>IB Film can be taken at either the standard level (SL) or higher level (HL). For both levels of IB Film, the candidate must research and write an Independent Study, do a Practical Project and give an oral presentation, based on the close analysis of a 5 minute extract from a film prescribed by the IBO.</p>
<p>The Independent Study is a research project that must be presented in the form of a Documentary Script, in which the candidate undertakes an individual investigation based on a topic of film history or theory. At SL, it should focus on a minimum of 2 films from different cultures, whereas at HL, it should focus on at least 4 films from different cultures. It is marked externally.</p>
<p>The Practical Project takes the form of a short film (4–5 minutes at SL and 6–7 minutes at HL) and a production portfolio and rationale about the work undertaken. The candidate must focus on one of five production roles: Director, Writer, Cinematographer, Editor or Sound Designer. Additionally, at HL, the candidate must produce an individual trailer of his or her film. (45 seconds-1 minute long). It is marked internally and moderated externally.</p>
<p>The Oral Presentation should focus on a 5-minute extract of a film set by the IBO, which also places the extract on a broader sociohistorical context and in terms of the film as a whole. The presentation lasts 10 minutes at SL and 15 minutes at HL. The IBO chooses 8 films (four from before 1960 and four from after 1960), from which the Film Teacher then chooses 3. The candidates then select from these three. The films analyzed cannot be studied in class.</p>
<p>Film SL is offered online to students enrolled in the IB Diploma Programme.</p>

<h3>Substituting courses from other subject groups</h3>
<hr>

<p>Group 6 subjects are considered electives, thus an IB Diploma candidate may substitute a variety of courses from other subject groups in lieu of taking a Group 6 course. This would result in a student studying an extra language, taking an extra social science or experimental science course, or taking Further Mathematics HL (provided that student is already taking Mathematics HL). The Group 3 Information in a Global Society (ITGS) course would be taken only as sixth subjects, as they do not satisfy the IB Diploma requirements for their respective subject groups.</p>


