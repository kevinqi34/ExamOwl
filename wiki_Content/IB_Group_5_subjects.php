<p>In both courses, the Paper 1 and Paper 2 exams test students only on their knowledge of topics from the Core. In Mathematics HL, the Paper 3 exam tests students specifically on their knowledge of the material from the Option selected by the school. As of the May 2008 examinations, schools are required to register students for a specific Option in advance and the school is now provided only the examination paper for the registered Option. In order for a student to be awarded full marks for an answer on any of the Mathematics SL or HL exams, accurate work must be shown.</p>

<h3>Computer science SL and HL</h3>
<hr>

<p>Computer science is, from first exams in 2014, no longer an elective course in group 5 but a full course in group 4, and has an updated syllabus and assessment scheme. The courses' focus shifted away from "program construction in Java" and "computational thinking" now lies at the core of the course.</p>


