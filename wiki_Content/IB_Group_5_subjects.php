<h3>Mathematics SL and HL</h3>
<hr>

<p><b>Core curriculum for Mathematics SL and HL</b></p>

<p>Mathematics SL is primarily intended for students who "expect to need a sound mathematical background as they prepare for future studies in subjects such as chemistry, economics and business," and its curriculum is a proper subset of the Mathematics HL curriculum. Topics from the following areas form the common "Core curriculum" for both courses:</p>


<ul>
<li>Algebra</li>
<li>Functions and equations</li>
<li>Circular functions and trigonometry</li>
<li>Vectors</li>
<li>Statistics and probability</li>
<li>Calculus</li>

</ul>
<p>In Mathematics SL, students are provided a minimum of 140 instructional hours on the topics from the Core.</p>

<p><b>Additional requirements for Mathematics HL</b></p>

<p>Mathematics HL is primarily intended for students "with a good background in mathematics who are competent in a range of analytical and technical skills," and who anticipate that mathematics will be "a major component of their university studies, either as a subject in its own right or within courses such as physics, engineering and technology." Therefore, HL students study the Core topics in greater depth for a minimum of 190 instructional hours and study at least one of the following optional topics ("Options"), for a minimum of 40 additional instructional hours:</p>


<ul>

<li>Further statistics and probability</li>
<li>Sets, relations, and groups</li>
<li>Calculus, formerly Series and differential equations</li>
<li>Discrete mathematics</li>


</ul>

<p><b>Assessment</b></p>

<p>In both courses, there is an <b>internal assessment</b> composed of mathematical investigation paper (formerly a portfolio of two papers), which is worth 20% of the student's final mark. The project is intended to comprise a combined total of 10 hours of the overall classroom time for both courses.</p>

