<p><b>The Group 5: Mathematics subjects</b> of the IB Diploma Programme consist of four different mathematics courses. To earn an IB Diploma, a candidate must pass one of the following three mathematics courses: Mathematical Studies SL (Standard Level), Mathematics SL or Mathematics HL (Higher Level). Further Mathematics HL can be taken as an elective in addition to Mathematics HL. The Computer science SL and HL courses, which were elective courses in group 5, is, for first examinations in 2014, a full course in group 4.</p>



<h3>Mathematical Studies SL</h3>
<hr>

<p><b>Curriculum</b></p>

<p>The Mathematical Studies SL course "is designed to build confidence and encourage an appreciation of mathematics in students" who do not expect mathematics to be a major component of their university studies. Its curriculum consists of the following topics:</p>



<ul>

<li>Numbers and algebra</li>
<li>Sets, logic, and probability</li>
<li>Functions</li>
<li>Geometry and trigonometry</li>
<li>Statistics</li>
<li>Introductory differential calculus</li>
<li>Financial mathematics</li>

</ul>


<p><b>Assessment</b></p>

<p>The <b>internal assessment</b> for this course comprises 20% of the final mark and consists of a project that is "an individual piece of work involving the collection of information or the generation of measurement." The project is intended to make up 20 hours of the overall classroom time for the course.</p>

