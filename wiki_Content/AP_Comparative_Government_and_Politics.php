<h3>Exam Changes</h3>

<hr>

<p>In order to better match a typical introductory college course, several changes were made to the course and the exam in the fall of 2005. These changes include:</p>

<ul>
<li>Greater emphasis on analysis of concepts and themes</li>
<li>Shifting focus to coverage of six core countries (China, Great Britain, Iran, Mexico, Nigeria, and Russia)</li>
<li>Emphasis on themes such as citizen-state relations, democratization, globalization, political change, and public policy</li>
</ul>








