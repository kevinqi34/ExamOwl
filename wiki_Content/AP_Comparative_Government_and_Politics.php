<p><b>Advanced Placement Comparative Government and Politics (or AP Comparative Government and Politics)</b> is an examination given by the College Board through the Advanced Placement Program. It tests topics commonly taught in introductory college comparative government or comparative politics courses.</p>


<h3>Exam Topics</h3>
<hr>
	
		<p>The countries whose politics, political institutions, policy making, and political cultures are examined by this AP test are:</p>
			<ul>
				<li>Nigeria Federal Republic of Nigeria (Nigeria)</li>
				<li>Iran Islamic Republic of Iran (Iran)</li>
				<li>China People's Republic of China (China)</li>
				<li>Russia Russian Federation (Russia)</li>
				<li>United Kingdom United Kingdom of Great Britain and Northern Ireland (Great Britain)</li>
				<li>Mexico United Mexican States (Mexico)</li>
			</ul>
			
