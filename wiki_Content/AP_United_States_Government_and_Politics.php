<h3>Exam</h3>
<hr>
<p>The exam consists of two sections:</p>
<ul>
<li>Section I: Multiple-Choice (45 minutes, 60 questions)</li>
<li>Section II: Free-response (100 minutes, 4 questions)</li>
</ul>
<p>In all a total of 120 points are attainable, with each section being worth 60 points.</p>



