<p><b>Advanced Placement United States Government and Politics, also known as AP US Gov & Pol, AP US Gov, AP NSL, AP GOPO or AP Gov</b>, is a college-level course and examination offered to high school students through the College Board's Advanced Placement Program. This course surveys the structure and function of American government and politics that begins with an analysis of the United States Constitution, the foundation of the American political system. Because of the manifold topics (various court cases, legislation, politics) discussed throughout this college-level study, many students struggle and, like most AP exams, low score averages are not an anomaly. Students study the three branches of government, administrative agencies that support each branch, the role of political behavior in the democratic process, rules governing elections, political culture, and the workings of political parties and interest groups.</p>


