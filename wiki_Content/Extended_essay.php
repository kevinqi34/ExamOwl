<p>The <b>extended essay (EE)</b> is a mandatory core component of the IB Diploma Programme. It is a research paper of up to 4,000 words giving students an opportunity to conduct independent research or investigation on a topic that interests them. Like the theory of knowledge (TOK) essay, TOK presentation and participation in creativity, action, service activities, submitting an extended essay is a prerequisite for award of the Diploma.</p>

<h3>Recommended subjects</h3>
<hr>
<p>Although the extended essay may be written on a topic of the student's choice, it is recommended that it be taken from the field of one of the IB subjects being studied (e.g. the essay may be about a book that has not been studied as part of IB English). However, the topic must not be too broad or too narrow as to make it difficult to write 4,000 words, and the general subject must be taught under the IB diploma program by one of the members of staff at the high school (so that there is someone with expertise able to help). The subject (not topic) on which the extended essay is written is recommended to be one that the candidate has formally studied, but this is not required. Also, the EE may not be written across different subjects – it must concentrate on one subject only, unless the student is writing under the World Studies topic. However, some subjects include several disciplines, with an emphasis towards one. An example is the subject Societies, which can include chemistry, biology, psychology, etc. generally with an emphasis toward one discipline.</p>

<h3>Supervision</h3>
<hr>
<p>The supervisor provides the student with assistance in putting together their EE, including guiding them in finding a suitable research question and on how to acquire the necessary resources to complete the research (such as a specific resource material–often hard-to-find documents or books–or laboratory equipment). The supervisor may suggest improvements to a version of the EE, but must not be engaged in writing it. The IBO recommends that the supervisor spend approximately two to three hours in total with the candidate discussing the EE. Some schools allow their students to choose a supervisor from outside their school, provided that the student appoint a teacher from inside the school to handle required administrative paperwork (such as anti-plagiarism policies).</p>

<h3>Assessment</h3>
<hr>

<p>Extended essays are marked by external assessors (examiners appointed by the IB) on a scale of 0 to 36. There are "general" and "subject-specific" criteria, at a ratio of 2:1 (24 possible marks for the general criteria and 12 marks for the subject-specific one). The total mark is converted into a grade from A to E. A similar system is used for theory of knowledge and students can gain up to 3 points for the diploma based on the grades achieved for EE and TOK. A student who scores an E on either the extended essay or TOK essay will still be eligible to receive an IB diploma, but will need to earn 28 points overall in their other subjects (EE Subject Guide p15).</p>


