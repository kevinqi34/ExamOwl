<p><b>Advanced Placement French Literature (or AP French Literature)</b> was an Advanced Placement course and examination offered by the College Board. The course was designed to replicate a college French literature course for high school students. Students studied a variety of novels, plays, and poetry, all written in French.</p>
<p>Due to low numbers of students taking AP French Literature, it was discontinued after the 2008-2009 year.</p>


<h3>Required Reading</h3>
<hr>
<p>The works required by the College Board for the 2008 exam were:</p>
<ul>
<li>Moderato Cantabile by Marguerite Duras</li>
<li>Le Cid by Pierre Corneille</li>
<li>Candide by Voltaire</li>
<li>Une Tempête (A Tempest) by Aimé Césaire</li>
<li>Pierre et Jean (Pierre and Jean) by Guy de Maupassant</li>
<li>L'École des femmes (The School for Wives) by Molière</li>
<li>Poems by Charles Baudelaire, Jean de la Fontaine, Joachim du Bellay, Guillaume Apollinaire, and Louise Labé.</li>
</ul>

