<h3>Exam</h3>
<hr>


<p>The AP Art History exam is divided into two sections: multiple choice, and free-response, with both sections having two parts.</p>

<p>The multiple choice section includes 115 questions that must be answered in 60 minutes. In Part A, students have 20 minutes to answer five sets of questions based on color images. In Part B, there are 85 questions that must be answered in 40 minutes, some of which do not pertain to any specific image, and involve the basic recalling of facts. Other questions, refer to black and white images within the test booklet.</p>

<p>The free-response section includes two 30-minute essay questions (Part A) and six 10-minute essay questions (Part B). In Part A, Students must respond to the prompts, citing two examples of works of art that reflect the prompt's theme. In Part B, each short essay question is based on a work of art and / or quotation from a primary source / document. For quotation-based questions, students must select an appropriate work of art to analyze.</p>

<h3>Scoring</h3>
<hr>

<p>The multiple choice section of the exam is worth 40% of a student's score. The free-response is worth 60%. Each correctly answered multiple choice question is worth one point. As of 2011, wrong and omitted questions do not affect the raw score. For the free-response section, the six short answers are worth 35% of the total grade, and each is graded on a scale of 0 to 4. Finally, the two long essays are each graded on a scale of 0 to 9, totaling 25% of the grade.</p>

<p>While the criteria varies from year to year, receiving a "5" from the ETS usually entails earning around 70% of the total points on the test, or having a raw score of 140.</p>


