<p><b>Advanced Placement Art History (AP Art History)</b> is an Advanced Placement Art History course and exam offered by College Board. AP Art History is designed to allow students to examine major forms of artistic expression relevant to a variety of cultures evident in wide variety of periods from present times into the past. Students acquire an ability to examine works of art critically, with intelligence and sensitivity, and to articulate their thoughts and experiences. The main topic is European Art, with focuses on African, Hindu, South and East Asian, and Islamic Art. Pre-Historic art has been removed from the curriculum. Starting in the 2015-2016 school year, College Board has introduced a new curriculum and exam for students to apply art historical skills to questions.</p>


