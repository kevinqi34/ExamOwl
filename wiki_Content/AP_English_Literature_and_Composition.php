<p><b>Advanced Placement English Literature and Composition (or AP English Literature and Composition, AP Lit and Comp, Senior AP English, AP Lit, or AP English IV)</b> is a course and examination offered by the College Board as part of the Advanced Placement Program.</p>


<h3>The Course</h3>
<hr>

<p>This course is designated for motivated students with a command of standard English, an interest in exploring and analyzing challenging classical and contemporary literature, and a desire to analyze and interpret dominant literary genres and themes. The AP English Literature Course is often offered to high school seniors and the other AP English course, AP English Language and Composition, to juniors. The College Board does not restrict courses by grade. Students learn and apply methods of literary analysis and write with a variety of purposes to increase precision in expression. Students in AP English Literature and Composition typically sit for the national AP examination administered each May for the College Board by the Educational Testing Service. The College Board publishes changing information about all AP courses and examinations on its web site.</p>
<p>On one of the three essays students write as part of the examination, students choose a work of literature they will write about. Readers of the exam who get an essay on a work they have not read typically pass the essay to a reader who has. The scoring guides that readers use to rate the essays are developed by experienced readers on site just before the reading begins each June, using some of the actual exam essays. Since those scoring guides do not exist before the Reading, instructors cannot teach to them but focus instead on encouraging text-based analysis.</p>
<p>The course is designed to be challenging and "arduous". Many students go on to perform well in college English courses as a result of the experience with the high school AP English class.</p>


<h3>Commonly Read Literary Works</h3>
<hr>
<p>The College Board publishes a recommended reading list, while emphasizing that it "does not mandate any particular authors or reading list." The reading list contains four major categories:</p>

<ul>
<li>Poetry, ranging from the 16th century (William Shakespeare) to contemporary poets (Seamus Heaney);</li>
<li>Drama, ranging from Greek tragedies (Aeschylus) to post-modern absurdists (Tom Stoppard);</li>
<li>Fiction – novels and short stories, from the 18th century comedies of manner of Jane Austen to the famous "Lost Generation" of F. Scott Fitzgerald and Ernest Hemingway;</li>
<li>Expository prose (essays), including Ralph Waldo Emerson and George Orwell.</li>
</ul>


