<h3>AP Computer Science exam</h3>
<hr>
<p>Since 2003, the AP Computer Science exam has tested students on their knowledge of computer science through Java. Before 1999, the AP exam tested students on their knowledge of Pascal. From 1999 to 2003, the exam tested students on their knowledge of C++ instead. The AP exam in Computer Science was first offered in 1984.</p>

<p>The exam is composed of two sections:</p>
<ul>
<li>Section I: Multiple Choice [1 hour and 15 minutes for 40 multiple-choice questions]</li>
<li>Section II: Free-Response [1 hour and 45 minutes for 4 problems involving extended reasoning]</li>
</ul>


<h3>Case Studies and labs</h3>
<hr>

<p>Historically, the AP exam has used several programs in its free-response section to test students' knowledge of object-oriented programs without requiring them to develop an entire environment. This practice has been discontinued as of the 2014-15 school year. There will be no question on the specific content of the labs on the AP exam. Instead, there will be questions that test the concepts developed in the labs. The College Board has instead created three new labs which instructors are invited to use, but they are optional and are not tested on the exam. These are three labs:</p>
<pre>
(1) The Magpie Lab
(2) The Elevens Lab
(3) The Picture Lab
</pre>


