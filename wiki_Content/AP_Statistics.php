<h3>The Course</h3>
<hr>
<p>If the course is provided by their school, students normally take AP Statistics in their junior or senior year and may decide to take it concurrently with a pre-calculus course. This offering is intended to imitate a one-semester, non-calculus based college statistics course, but high schools can decide to offer the course over one semester, two trimesters, or a full academic year.</p>
<p>The six-member AP Statistics Test Development Committee is responsible for developing the curriculum. Appointed by the College Board, the committee consists of three college statistics teachers and three high school statistics teachers who are typically asked to serve for terms of three years.</p>


<p><b>Curriculum</b></p>
<p>Emphasis is placed not on actual arithmetic computation, but rather on conceptual understanding and interpretation. The course curriculum is organized around four basic themes; the first involves exploring data and covers 20–30% of the exam. Students are expected to use graphical and numerical techniques to analyze distributions of data, including univariate, bivariate, and categorical data. The second theme involves planning and conducting a study and covers 10–15% of the exam. Students must be aware of the various methods of data collection through sampling or experimentation and the sorts of conclusions that can be drawn from the results. The third theme involves probability and its role in anticipating patterns in distributions of data. This theme covers 20–30% of the exam. The fourth theme, which covers 30–40% of the exam, involves statistical inference using point estimation, confidence intervals, and significance tests.</p>

<h3>The Exam</h3>
<hr>
<p>Along with the course curriculum, the exam is developed by the AP Statistics Test Development Committee as well. With the help of other college professors, the committee creates a large pool of possible questions that is pre-tested with college students taking statistics courses. The test is then refined to an appropriate level of difficulty and clarity. Afterwards, the Educational Testing Service is responsible for printing and administering the exam.</p>
<p><b>Structure</b></p>
<p>The exam is offered every year in May. Students are not expected to memorize any formulas; rather, a list of common statistical formulas related to descriptive statistics, probability, and inferential statistics is provided. Moreover, tables for the normal, Student's t and chi-squared distributions are given as well. Students are also expected to use graphing calculators with statistical capabilities. The exam is three hours long with ninety minutes allotted to complete each of its two sections: multiple choice and free-response. The multiple choice portion of the exam consists of forty questions with five possible answers each. The free response section contains six open-ended questions that are often long and divided into multiple parts. The first five of these questions may require twelve minutes each to answer and normally relate to one topic or category. The sixth question consists of a broad-ranging investigative task and may require approximately twenty-five minutes to answer.</p>
<p><b>Grading</b></p>
<p>The multiple choice section is scored immediately after the exam by computer. One point is awarded for each correct answer, no points are credited or deducted for unanswered questions, and points are no longer deducted for having an incorrect answer.</p>
<p>Students' answers to the free-response section are reviewed in early June by readers that include high school and college statistics teachers gathered in a designated location. The readers use a pre-made rubric to assess the answers and normally grade only one question in a given exam. Each question is graded on a scale from 0 to 4, with a 4 representing the most complete response. Communication and clarity in the answers receive a lot of emphasis in the grading.</p>
<p>Both sections are weighted equally when the composite score is calculated. The composite score is reported on a scale from 1 to 5, with a score of 5 being the highest possible.</p>



