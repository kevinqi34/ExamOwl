<p><b>Creativity, Activity, Service (CAS) (Formerly 'Creativity, Action Service')</b> is a mandatory core component of the IB Diploma Programme. It aims to provide a 'counterbalance' to the academic rigour of the educational programme. Before the 2010 examination there was a 150 hour requirement, with an approximately equal distribution of creativity, activity, and service. This was mainly done for two reasons: to ensure that students engage in meaningful activities and to decrease the amount of CAS fraud (i.e. claiming hours which have not been completed). Students are now expected to have two CAS activities for each CAS category and students need to prove that they are participating in CAS activities on a daily basis (though not necessarily all CAS categories each week). Moreover, students must have one CAS project which spans more than three months and bridges two CAS components e.g. creativity and service. Finally one must prove that the CAS activities have resulted in the eight projected outcomes of the project (one CAS activity can have multiple outcomes).</p>
<h3>CAS outcomes</h3>
<hr>

<ul>
<li>Increase awareness of your strengths and areas for growth</li>
<li>Undertake new challenges</li>
<li>Plan and initiate activities</li>
<li>Work collaboratively with others<li>
<li>Show perseverance and commitment<li>
<li>Engage with issues of global importance<li>
<li>Consider ethical implications<li>
<li>Develop new skills<li>


</ul>


<h3>CAS aims</h3>
<hr>

<p>The function of CAS is to allow IB schools the opportunity to "give students the means to learn through experience [and] how to take actions in the service of others."</p>
<p>All the portions (creativity, action, and service) are vaguely defined and should, according to the International Baccalaureate Organisation, be interpreted as imaginatively as possible, so that a wide array of different activities can qualify for CAS. The learning outcomes and the quality of the CAS activity are of utmost importance.</p>

<h3>Documentation</h3>
<hr>

<p>While individual schools or regions have different methods of documentation, all require some form of proof or the signature of the activity coordinator to be presented after completion of the activity. Students must also personally keep track of their total records in either a log or "diary" format. This allows for students to monitor their required hours and to present a coherent documentation of their entire CAS experience.</p>
<p>CAS performance and records are documented by the student using official forms (CAS/CP) that are submitted to the IB regional offices at the beginning of the examination session.</p>

<h3>Assessment</h3>
<hr>
<p>CAS is internally assessed. However, IBO can ask for a number of random samples to be submitted. A failure to complete and provide evidence for completion is considered a failing condition for the entire Diploma (although candidates are given another year to finish their CAS program if they fail, and if they complete it in this time they will receive their diploma).</p>

