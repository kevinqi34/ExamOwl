<p><b>Advanced Placement (AP) Spanish Literature</b> is a high school course and examination offered by the College Board's Advanced Placement Program.</p>

<h3>The Course</h3>
<hr>
<p>This course is based on improving skills in writing Spanish, and critical reading of advanced Spanish and Latin American literature. It is typically taught as a Spanish V or VI course. The AP Spanish Literature course is designed to be comparable to a third-year College/University introduction to Hispanic literature course. Students concentrate on developing proficiency in reading and writing in preparation for the AP Spanish Literature examination. In addition, this course will emphasize mastery of linguistic competencies at a very high level of proficiency.</p>
<p>The course has a required reading list which draws from a variety of sources including well-known authors and poets as Federico García Lorca, Miguel de Cervantes, Tirso de Molina, Gabriel García Márquez, Alfonsina Storni, Juan Rulfo, Isabel Allende and Miguel de Unamuno, as well as a variety of lesser known short stories, poems, and novels. A complete list of the works read is regulated annually by the College Board. The course should be taught entirely in Spanish although English translations of the stories are often common resources for the students.</p>


<h3>The Exam</h3>
<hr>
<p>The exam is divided into two sections with several parts. Section one is split into two parts: Part A consists of listening with a recited poem, an recording of an interview of an author, and a presentation of a topic in relation to the course (15 questions) while Part B is reading comprehension based on a wide variety of works—even a literary criticism (50 questions). 80 minutes is given for the whole section.</p>
<p>Section two is free-response, and contains two short answer questions and two long answer questions. The first question is based on an excerpt of a work and students must identify the author, the time period, and most importantly elaborate on the theme presented in the passage (15 minutes). The second question asks the student to read an excerpt from a work and compare the theme to that of the accompanying image of a piece of artwork (15 minutes). The third question tests the student on how an excerpt of a piece of literature represents the genre or movement to which it belongs (35 minutes). Finally, the fourth question directs the student to analyze the literary techniques in two excerpts that the authors of two works utilized to convey a theme (35 minutes). 100 minutes is allotted for this section.</p>
<p>The test is approximately three hours in duration.</p>

