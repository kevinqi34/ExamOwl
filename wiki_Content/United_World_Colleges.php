<p><b>United World Colleges (or UWC)</b> is an education movement comprising 15 international schools and colleges, national committees in more than 140 countries, and a series of short educational programmes. Students are selected from around the globe based on their merit and potential. UWC schools, colleges and national committees offer scholarship and bursary schemes as well as accepting a number of fee-paying students that varies by college.</p>
<p>The UWC international organisation is a British-based foundation and has 15 schools and colleges in Canada, Hong Kong, India, Italy, Norway, Singapore, Swaziland, the United States, the United Kingdom, Germany, Armenia, Costa Rica, Bosnia and Herzegovina, China and the Netherlands; national committees in more than 140 countries; a portfolio of short programmes running in numerous countries; a network of more than 50,000 alumni from more than 181 countries; and an international office in London. The mission is to make education a force to unite people, nations and cultures for peace and a sustainable future.</p>
<p>UWC colleges teach the International Baccalaureate, with three schools in Singapore, the Netherlands and Swaziland which, on top of the IB, also teach a pre-16 syllabus to younger students. The now-closed UWC vocational college in Venezuela accepted students at tertiary level and taught a Higher Diploma in Farm Administration. Each UWC typically comprises between 200 and 300 students from about 85 countries.</p>

<h3>History</h3>
<hr>

<p>The first UWC college, the United World College of the Atlantic, located in a 12th-century castle set on 90 hectares of grounds in the Vale of Glamorgan in South Wales, United Kingdom, was founded in 1962 with the initiative of Kurt Hahn, a German educationalist who had previously founded Schule Schloss Salem in Germany, Gordonstoun in Scotland, and the Outward Bound movement; the castle was gifted to UWC by Antonin Besse II, the son of Sir Antonin Besse. Kurt Hahn's vision was based on his post-war experience at the NATO Defence College, where he had observed discussion and collaboration between former enemies. He wanted to transmit a spirit of mutual understanding to young people to help them overcome prejudice and antagonism through living and working together.</p>
<p>Hahn envisaged a college educating boys and girls of age 16 to 20. The selection would be based on personal motivation and potential, regardless of any social, economic or cultural factors. A scholarship programme would facilitate recruitment of young people from different economic backgrounds. The project was realised in 1962 with the inauguration of Atlantic College in Wales.</p>
