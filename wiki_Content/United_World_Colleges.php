<p>The current UWC president is Queen Noor of Jordan (1995–present), a role she shared with former South African President Nelson Mandela until his death in December 2013. Former UWC presidents have included Lord Mountbatten (1967-1978) and Prince Charles (1978-1995).</p>
<p>The philosophy of the schools is in accordance with a thought from Nobel Peace Prize laureate Lester B. Pearson: "How can there be peace without people understanding each other; and how can this be if they don't know each other?"</p>


<h3>Academics</h3>
<hr>
<p>UWC schools and colleges offer two years of pre-university education (with the exception of the now closed Simón Bolívar United World College of Agriculture in Venezuela which offered an agricultural diploma), the United World College of South East Asia in Singapore which offers kindergarten through grade 12 on its two campuses, and the United World College Maastricht, which offers pre-school through grade 12. After graduation an UWC alumni are holders of the International Baccalaureate Diploma, a high school diploma recognised worldwide. The International Baccalaureate has its headquarters in Geneva, Switzerland, while its Examinations Office is in Cardiff, United Kingdom, in part due to the influence of nearby United World College of the Atlantic in its early development.</p>
<p>The three working languages of the International Baccalaureate are English, French and Spanish. Eleven of the twelve UWC schools and colleges use English as the main language of teaching and communication. UWC of the Adriatic in Italy require that all students study Italian respectively in order to facilitate their relationship with the local populations. The teaching in the Simón Bolívar United World College of Agriculture in Venezuela was in Spanish, with English language classes. That college was attended by slightly older students and offered a diploma in agricultural administration.</p>
<p>UWC students are eligible, after graduation, to participate in the Shelby Davis Scholarship programme, which funds undergraduate study (based on need) for UWC students at 91 universities in the United States.</p>


<h3>Activities</h3>
<hr>
<p>The CAS (Creativity, Action, Service) programme – one of the requirements of the IB Diploma – is a part of UWC system. CAS and the IB programme have their roots at the United World College of the Atlantic. During the creation of the IB programme, the academic and social lives of students at Atlantic College were taken as examples.</p>
<p>Special activities at UWC schools and colleges include the Coral Monitoring Service at Li Po Chun United World College and the student-run kitchen garden at United World College of the Atlantic. At Mahindra United World College of India students fight fires in order to protect the school's local biodiversity reserve. At the United World College in Mostar the CAS Program contributes to the restoration of the divided post-conflict Mostar society. At the Armand Hammer United World College of the American West students can take part in the Bartos Institute for Constructive Engagement of Conflict, Wilderness First Aid certification course or engage in Southwest Studies exploring New Mexico’s local treasures. At Pearson College UWC students help manage the Race Rocks Marine Protected Area, bake bread and kayak.</p>


<h3>Admissions</h3>
<hr>
<p>Entry into a UWC school or college is based on a students' commitment to UWC values and how suited they are to champion UWC's mission. Many UWC students are awarded full or partial scholarships through the national committee system, while others are supported directly by the school or college to which they are admitted. UWC national committees are located in roughly 140 countries; some are run completely by volunteers, while others have permanent staff.</p>
<p>Applicants for UWC scholarships are generally selected by national committees. They have a slightly varying selection system, but are unified by the UWC mission and values and recognized by the UWC international office, and by extension, by the colleges themselves. In Egypt, for example, the places are offered on the basis of a system of national competitions and specialized interviews, whereas in India, Portugal, Brazil, Argentina, the United Kingdom, Spain, Germany and Italy shortlisted applicants attend a two-day residential selection with an interview, games and debates. In other countries, such as Hong Kong and Colombia, applicants are invited to attend a day-camp named "Challenge Day" where they engage in activities such as debate, learning a new language, and group games. Shortlisted applicants then attend a final interview before gaining admission.</p>



