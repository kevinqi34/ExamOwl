<p><b>AP Physics B</b> was an Advanced Placement science course in which high school students studied Newtonian mechanics, electricity and magnetism, fluid mechanics, and thermal physics, waves and optics, and atomic and nuclear physics in preparation for a cumulative exam given each May. The course was non-calculus-based and involved algebra and trigonometry to solve various physics problems. It was supposed to be equivalent to a year-long introductory college course in physics.</p>
<p>This course also helped prepare students for the SAT Subject Test in Physics, which is also administered by College Board.</p>
<p>This course was discontinued and replaced with AP Physics 1 and AP Physics 2 beginning in the 2014–2015 school year. The last administration of the AP Physics B exam was in May 2014.</p>

<h3>Exam</h3>
<hr>
<p>The exam was divided into two sections, a multiple choice section (70 questions) and a free response section (6–7 questions). Students had to complete each section within 90 minutes. To test knowledge as well as skills, they took the multiple choice section without a calculator. The free response section would permit the use of a calculator as well as a list of common formulas. The scoring was weighted such that each section was worth fifty percent of the final score. Overall, the exam was configured to approximately cover a set percentage of each of the five target categories:</p>
