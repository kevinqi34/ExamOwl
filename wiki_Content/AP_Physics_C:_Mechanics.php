<p><b>AP Physics C: Mechanics</b> is an Advanced Placement science course that studies Newtonian mechanics. Methods of calculus are used wherever appropriate in formulating physical principles and in applying them to physical problems. It is supposed to be equivalent to an introductory college course in mechanics for physics or engineering majors. This course, taken along with courses covering other areas, such as Electricity and Magnetism, Waves, Thermodynamics, and Modern Physics can help prepare students for the SAT Subject Test in Physics, also administered by College Board. This course is often combined with AP Physics C: Electricity and Magnetism to make a unified Physics C course that prepares for both exams, or it may be a separate course. In the former scenario, Electricity and Magnetism is typically taught second, as it requires much of the knowledge gained in the Mechanics course.</p>

<h3>The Exam</h3>
<hr>
<p>The exam is configured in two categories, a thirty-five (35) question multiple choice section and a three (3) question free response section. Test takers are allowed to use a calculator on the entire AP Physics C: Mechanics Exam – including both the multiple-choice and free response sections. The test is weighted such that each section is worth fifty percent (50%) of the final score. It is the shortest AP exam currently administered, with total time at 90 minutes.</p>
