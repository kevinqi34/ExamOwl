<p><b>AP Studio Art</b> is a series of Advanced Placement Courses divided into three different categories: AP Studio Art Drawing, AP Studio Art 2D Design, and AP Studio Art 3D Design.</p>

<h3>The Portfolio</h3>
<hr>
<p>Unlike traditional AP Exams that utilize a multiple-choice section, free response section, and occasionally an audio section, the AP Studio Art Exam is a portfolio that encompasses 3 different categories: Quality, Concentration, and Breadth. Depending on the AP Studio Art exam the person is taking, the components for each of the 3 categories will vary. Regardless of the exam, all AP Studio Art portfolios have to be turned in by a set date and time, for example, 12:00pm on May 11, 2007 for the 2007 exam.</p>

<h3>AP Studio Art Drawing</h3>
<hr>
<p>AP Studio Art Drawing is an advanced placement course that deals with basic painting and drawing. Focus is applied on the composition of the different lines/colors/shape/etc... of the painting instead of the design itself. Originally called AP Studio Art, it was later changed to AP Studio Art Drawing.</p>
<p><b>Portfolio</b></p>

<ul>
<li><b>Section I: Quality:</b> A student submits 5 actual artwork to the AP College Board that represent the student's quality of technique and their design concepts.</li>
</ul>


<ul>
<li><b>Section II: Concentration:</b> A student submits 12 different slides that demonstrate the student's ability to paint and draw a variety of pieces that relate to 1 idea. These are arranged in a special order on slide coverings from College Board. Starting 2009, an online application replaced the use of physical slides and associated documents.</li>
</ul>

<ul>
<li><b>Section III: Breadth:</b> A student submits 12 additional and different slides that demonstrates the student's ability to incorporate drawing techniques and issues that include drawing from observation, work with inverted or nonobjective forms, effective use of light and shade, line quality, surface manipulation, composition, various spatial systems, and expressive mark-making. There are arranged similar to Section II. Starting 2009, an online application replaced the use of physical slides and associated documents.</li>
</ul>

