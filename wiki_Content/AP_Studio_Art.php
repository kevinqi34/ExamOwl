<h3>AP Studio Art 2D</h3>
<hr>


<p>AP Studio Art 2D is an advanced placement course that is similar to AP Studio Art Drawing. It deals with two-dimensional applications such as graphic design, photography, weaving, and collage. As a contrary to AP Studio Art Drawing, focus is applied on the design itself instead of the composition of the artwork.</p>
<p><b>Portfolio</b></p>
<ul>
<li><b>Section I: Quality:</b> A student submits 5 actual artwork that represents the student's quality of designing pieces in 2D.</li>
</ul>
<ul>
<li><b>Section II: Concentration:</b> A student submits 12 different slides that demonstrate the student's ability to make a variety of pieces in 2D that relate to 1 idea. These are arranged in a special order on slide coverings from College Board. Starting 2009, an online application replaced the use of physical slides and associated documents.</li>
</ul>
<ul>
<li><b>Section III: Breadth:</b> A student submits 12 additional and different slides that demonstrate the student's ability to incorporate 2D Design principles that include unity/variety, balance, emphasis, contrast, rhythm, repetition, proportion/scale, and figure-ground relationship. These are arranged similar to Section II. Starting 2009, an online application replaced the use of physical slides and associated documents.</li>
</ul>

<p><b>Grade Distribution</b></p>
<p>In the 2012 administration 23,591 students took the exam with a mean score of 3.28.</p>
