<h3>AP Studio Art 3D</h3>
<hr>
<p>AP Studio Art 3D is a three-dimensional Advanced Placement Studio Art course that holds many similarities to the AP Studio Art 2D course. The course deals with three-dimensional artistic applications such as metalworking, sculpture, model, and ceramics. Like AP Studio Art 2D, the focus on the design of the artwork itself as opposed to its composition.</p>
<p><b>Portfolio</b></p>
<ul>
<li>Section I: Quality: The actual work for this exam is three-dimensional and not flat, so transporting it directly to The College Board could cause damage to the artwork and the pieces would most likely not fit in portfolios. Because of this, five pieces of artwork are shown in ten slides to showcase the quality of the student's three-dimensional pieces.</li>
</ul>
<ul>
<li>Section II: Concentration: 12 different slides are submitted to demonstrate the student's ability to produce a variety of pieces that relate to a single idea. The slides are arranged in slide coverings in an order designated by The College Board. Starting 2009, an online application replaced the use of physical slides and associated documents, and the student was allowed to determine the order of the works in their concentration.</li>
</ul>
<ul>
<li>Section III: Breadth: Eight different works are shown in 16 slides which are meant to demonstrate the student's ability to incorporate a variety of different 3D Design principles, including unity/variety, balance, emphasis, contrast, rhythm, repetition, proportion/scale, and figure/ground relationship.Similar to Section II, this section's slides are also arranged in an order designated by The College Board. Starting 2009, an online application replaced the use of physical slides and associated documents.</li>
</ul>


