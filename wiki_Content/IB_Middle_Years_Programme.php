<p><b>The International Baccalaureate Middle Years Programme (MYP)</b> is an educational program affiliated with the International Baccalaureate programme, intended for students aged approximately 11 to 16 (grades 6–10 in International Schools, the United States, Canada, New Zealand, Switzerland and Australia). Thus, in the United States the program is often taught throughout the middle school years and the first two years of high school. Typically, middle schools and high schools work in coordination with each other when the program can not be entirely hosted within one combined school. The full program lasts 5 years although shorter programs can be adopted with permission from the International Baccalaureate Organisation (IBO). Official MYP documentation is available from the IB in English, French, Spanish, Russian and Chinese.</p>

<h3>Curricula Framework</h3>
<hr>
<p><b>Subject groups</b></p>
<p>The subjects taught in the MYP are divided into eight subject groups:</p>
<ul>
<li>Language and Literature ("Language A" before 2014)</li>
<li>Language Acquisition ("Language B" before 2014)</li>
<li>Individuals and Societies ("Humanities" before 2014)</li>
<li>Sciences</li>
<li>Mathematics</li>
<li>The Arts</li>
<li>Physical and Health Education ("Physical Education" before 2014)</li>
<li>Design ("Technology" before 2014)</li>
</ul>

<p>Schools can choose the subjects they teach within each subject group. However, each subject group must receive a minimum of 50 hours of curriculum time each academic year. A student could take two or more "Language and Literature" courses if they have sufficient proficiency in both. Schools are given much flexibility to allow them to introduce subjects which they consider important, and to organize their own student assessment and reporting procedures. However, the MYP gives clear exit criteria for each subject group for MYP 5 (grade 10).</p>
<p>The program is based around five "Areas of Interaction": approaches to learning (related to study skills), community and service, human ingenuity, environments, and health and social education. The areas of interaction are considered a key feature of the MYP. They are not generally taught as separate courses, but rather as themes that are reflected in all subjects through unit questions. The community and service area requires students to study and perform community studies and service throughout the program.</p>
<p>In addition to the eight subject groups, in MYP 5, students complete a long-term project on a topic of their choice, with teacher supervision.</p>
<p>Schools can opt for external moderation so that students may receive an IB MYP Certificate.</p>
<p>In order to get an MYP Certificate in MYP 5, the student must score at least a 2 in each subject and a 3 in Personal Project. Moreover, the student must score at least a total score of 36 out of 63 in 7 subjects (average of 4).</p>

<p><b>Assessment Criteria</b></p>
<p>In MYP, there are different assessment criteria in different subject groups. At the end of the year, the subject teacher will have to give a final score (not average) based on the performance throughout the whole year and decide a score out of 7. From that, it is inferred that the final year exam does not take up a portion larger than the rest of the tasks and tests and will not affect greatly on the decision of the final grade.</p>
<p>For example, the criteria of Science (since 2014) is</p>
<ul>
<li>A - Knowing and Understanding</li>
<li>B - Inquiring and Designing</li>
<li>C - Processing and Evaluating</li>
<li>D - Reflecting on the Impacts of Science</li>
</ul>

<p>Currently, all the Science criteria are scored out of 8.</p>
<p>Unlike other international programmes such as iGCSE, MYP puts greater emphasis on different types of assessments comparatively.</p>

<p><b>Learner Profile</b></p>

<p>At the center of the MYP is the <b>IB Learner Profile</b>, which defines the type of students all the IB programmes (PYP, MYP, and DP) are intended to develop.</p>
<ul>
<li>Caring</li>
<li>Balanced</li>
<li>Open-minded</li>
<li>Knowledgeable</li>
<li>Communicative</li>
<li>Risk-taking</li>
<li>Principled</li>
<li>Reflective</li>
<li>Inquiring</li>
<li>Thinker</li>
</ul>


<p><b>Areas of Interaction</b></p>
<p>There are five Areas of Interaction (AOIs) which are applied to every course the student takes. They are designed to help students recognize the connection between what they learn in the classroom and the world around them, to tie the various subject areas together, and eventually to help students "see knowledge as an interrelated, coherent whole."</p>

<p>The AOIs should be linked to every topic they learned in class and every assessment they do.</p>
<p>The Areas of Interaction are as follows:</p>
<ol>
<li><b>Approaches to Learning</b> – how people learn and communicate information</li>
<li><b>Community and Service</b> – relationships with and responsibility to other people</li>
<li><b>Human Ingenuity</b> – what people create and how, and its effect</li>
<li><b>Environments</b> – relationships with and responsibility to the world around us</li>
<li><b>Health and Social Education</b> – physical, social, and emotional well-being</li>
</ol>

<p><b>Personal Project</b></p>
<p>The "Personal Project" a culmination of student learning and a focus of the areas of interaction. Just as with the extended essay in the IB Diploma Programme, students are required to choose an academic or non-academic topic or subject for their project, which they are expected to complete over the course of the school year. Students are required to keep a personal journal while working on the process, and schedule regular meetings with an MYP teacher who will serve as their advisor throughout the year; in addition, a final reflection must be written upon the completion of the project which explains how it ties in with at least one of the Global Contexts. To get the MYP certificate, the candidate must get at least 3 out of 7 in the final score.</p>

<h3>Participation</h3>
<hr>
<p>In order to participate in the IB Middle Years Program, students must attend an authorised IB World School.</p>



