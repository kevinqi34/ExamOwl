<p><b>Advanced Placement English Language and Composition (commonly abbreviated to AP Lang)</b> is a course and examination offered by the College Board as part of the Advanced Placement Program.</p>

<h3>Course</h3>
<hr>
<p>AP English Language and Composition is a course in the study of rhetoric taken in high school. Many schools offer this course primarily to juniors and the AP English Literature and Composition course to seniors. Other schools reverse the order, and some offer both courses to both juniors and seniors. The College Board advises that students choosing AP English Language and Composition should be interested in studying and writing various kinds of analytic or persuasive essays on nonliterary topics, while students choosing AP English Literature and Composition should be interested in studying literature of various periods and genres and using this wide reading knowledge in discussions of literary topics.</p>

