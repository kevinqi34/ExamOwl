<p><b>Section I: Multiple-Choice</b></p>
<p>The multiple-choice section of the test is approximately 55 questions, with the exact number of questions varying with each test administration. There are typically 5 passages divided between pre-20th century non-fiction prose, and 20th and 21st century non-fiction prose. The questions typically focus on identifying rhetorical devices and structures from the passages, as well as their general functions, purposes in a passage, and the relationships between the devices. In 2007, questions were added that ask about citation information included in the passages. These citation questions are not designed to test knowledge about MLA, APA, Chicago Style, or any other particular citation format, but instead focus on how the citations reference and enhance information from the passage. Students have 60 minutes to answer all 55 questions.</p>

<p><b>Section II: Free-Response Writing</b></p>
<p>The Free-Response section of the test consists of three prompts, each of a different type: synthesis, passage analysis, and argument. Each is scored on a scale from 1 to 9.</p>
<p>With the introduction of the synthesis essay in 2007, the College Board allotted 15 additional minutes to the free-response exam portion to allow students to read and annotate the three prompts, as well as the passages and sources provided. Students may write notes in the prompt booklet about the material during those 15 minutes, but may not write in the essay booklets during this time. As the prompt booklets are not collected, any writing in the prompt booklet does not count when scoring the essays.</p>
<p>The synthesis prompt typically requires students to consider a scenario, then formulate a response to a specific element of the scenario using at least three of the accompanying sources for support. While a total of six or seven sources accompany the prompt, using information from all of the sources is not necessary, and may even be undesirable. The source material used must be cited in the essay in order to be considered legitimate.</p>
<p>The analysis prompt typically asks students to read a short (less than 1 page) passage, which may have been written at any time, as long as it was originally written in modern English. After reading the passage, students are asked to write an essay in which they analyze and discuss various techniques the author uses in the passage. The techniques differ from prompt to prompt, but may ask about strategies, argumentative techniques, motivations, or other rhetorical elements of the passage, and how such techniques effectively contribute to the overall purpose of the passage. The prompt may mention specific techniques or purposes, but some leeway of discussion is left to the student.</p>
<p>The argument prompt typically gives a position in the form of an assertion from a documented source. Students are asked to consider the assertion, and then form an argument that defends, challenges, or qualifies the assertion using supporting evidence from their own knowledge or reading.</p>


<p><b>Scoring</b></p>

<p>The multiple-choice section is scored by computer. Formerly, the test was scored by awarding 1 point for correct answers, while taking off a 1/4 point for incorrect answers. No points were taken away for blank answers. However, the College Board discontinued the policy for all AP Exams in 2011; now they only award 1 point for each correct answer, with no 1/4 point deductions.</p>
<p>The free-response section is scored individually by hundreds of educators each June. Each essay is assigned a score from 1-9, 9 being high. Scoring is holistic, meaning that specific elements of the essay are not assessed, but each essay is scored in its entirety.</p>
<p>The scores from the three essays are added and integrated with the adjusted multiple-choice score (using appropriate weights of each section) to generate a composite score. The composite is then converted into an AP score of 1-5 using a scale for that year's exam.</p>
<p>Students generally receive their scores by mail in mid-July of the year they took the test. Alternately, they can receive their scores by phone as early as July 1 for a fee.[4] Sub-scores are not available for students for the English Language and Composition Exam.</p>
<p>AP instructors receive a score sheet showing the individual score for each of their students, as well as some score information and national averages.</p>



