<p><b>Advanced Placement Macroeconomics (also known as AP Macroeconomics, AP Macro, or simply Macro)</b> is a course offered by the College Board as part of the Advanced Placement Program for high school students interested in college-level coursework in macroeconomics. Study begins with fundamental economic concepts such as scarcity, opportunity costs, production possibilities, specialization, comparative advantage, demand, supply, and price determination. Major topics include measurement of economic performance, national income and price determination, fiscal and monetary policy, and international economics and growth. AP Macroeconomics is frequently taught in conjunction with (and, in some cases, in the same year as) AP Microeconomics, although more students take the former.</p>


<h3>Topic Outline</h3>
<hr>

			<ul>	
				<li>Basic Economic Concepts (8%-12%)</li>
					<ul>
						<li>Scarcity, choice, and opportunity costs</li>
						<li>Production possibilities curve</li>
						<li>Comparative advantage, absolute advantage, specialization, and exchange</li>
						<li>Demand, supply, and market equilibrium</li>
						<li>Macroeconomic issues: business cycle, unemployment, inflation, growth</li>
					</ul>
				<li>Measurement of Economic Performance (12%-16%)</li>
					<ul>
						<li>National income accounts</li>
							<ul>
								<li>Circular flow</li>
								<li>Gross domestic product</li>
								<li>Components of gross domestic product</li>
								<li>Real versus nominal gross domestic product</li>
							</ul>
						<li>Inflation measurement and adjustment</li>
							<ul>
								<li>Price indices</li>
								<li>Nominal and real values</li>
								<li>Costs of inflation</li>
							</ul>
						<li>Unemployment</li>
							<ul>
								<li>Definition and measurement</li>
								<li>Types of unemployment</li>
								<li>Natural rate of unemployment</li>
							</ul>
					</ul>
				<li>National Income and Price Determination (10%-15%)</li>
					<ul>
						<li>Aggregate demand</li>
							<ul>
								<li>Determinants of aggregate demand</li>
								<li>Multiplier and crowding-out effects</li>
							</ul>
						<li>Aggregate supply</li>
							<ul>
								<li>Short-run and long-run analyses</li>
								<li>Sticky versus flexible wages and prices</li>
								<li>Determinants of aggregate supply</li>
							</ul>
						<li>Macroeconomic Equilibrium</li>
							<ul>
								<li>Real output and price level</li>
								<li>Short and long run</li>
								<li>Actual versus full-employment output</li>
								<li>Economic fluctuations</li>
							</ul>
						</ul>
					<li>Financial Sector (15%-20%)</li>
						<ul>
							<li>Money, banking, and financial markets</li>
								<ul>
									<li>Definition of financial assets: money, stocks, bonds</li>
									<li>Time value of money (present and future value)</li>
									<li>Measures of money supply</li>
									<li>Banks and creation of money</li>
									<li>Money demand</li>
									<li>Money market</li>
									<li>Loanable funds market</li>
								</ul>
							<li>Central bank and control of the money supply</li>
								<ul>
									<li>Tools of central bank policy</li>
									<li>Quantity theory of money</li>
									<li>Real versus nominal interest rates</li>
								</ul>
							</ul>
					<li>Inflation, Unemployment, and Stabilization Policies (20%-30%)</li>
						<ul>
							<li>Fiscal and monetary policies</li>
								<ul>
									<li>Demand-side effects</li>
									<li>Supply-side effects</li>
									<li>Policy mix</li>
									<li>Government deficits and debt</li>
								</ul>
							<li>Inflation and unemployment</li>
								<ul>
									<li>Types of inflation</li>
									<li>Demand-pull inflation</li>
									<li>Cost-push inflation</li>
									<li>The Phillips curve: short run versus long run</li>
									<li>Role of expectations</li>
								</ul>
						</ul>
					<li>Economic Growth and Productivity (5%-10%)</li>
						<ul>
							<li>Investment in human capital</li>
							<li>Investment in physical capital</li>
							<li>Research and development, and technological progress</li>
						</ul>
					<li>Open Economy: International Trade and Finance (10%-15%)</li>
						<ul>
							<li>Balance of payments accounts</li>
								<ul>
									<li>Balance of trade</li>
									<li>Current account</li>
									<li>Capital account</li>
								</ul>
							<li>Foreign exchange market</li>
								<ul>
									<li>Demand for and supply of foreign exchange</li>
									<li>Exchange rate determination</li>
									<li>Currency appreciation and depreciation</li>
								</ul>
							<li>Net exports and capital flows</li>
							<li>Links to financial and goods markets</li>
						</ul>
					</ul>

<h3>The Exam</h3>
<hr>
<ul>
<li>Section I: Multiple Choice (70 minutes - worth 2/3 of the exam grade)</li>
<li>Section II: Free-Response (60 minutes - worth 1/3 of the exam grade)</li>
</ul>
<p>Section I consists of 60 questions. The multiple-choice portion of the exam is constructed to reflect the weighting of topics on the AP Macroeconomics Topic Outline (above). For example, test-takers should expect to find between 3 and 6 questions of the 60 total questions on the multiple choice section pertaining to "economic growth" because Unit VI. Economic Growth and Productivity should comprise 5%-10% of the course material studied.</p>


