<p>The <b>International Baccalaureate Primary Years Programme (PYP)</b> is an educational programme managed by the International Baccalaureate (IB) for students aged 3 to 12. While the programme prepares students for the IB Middle Years Programme, it is not a prerequisite for it. The subject areas of the PYP are language, social studies, mathematics, science and technology, arts, and personal, social and physical education. Students are required to learn a second language during the programme. Assessment is carried out by teachers according to strategies provided by the IB, and with respect to guidelines to what the students should learn specified in the curriculum model.</p>

<h3>Mission Statement</h3>
<hr>
<p>The mission statement of the International Baccalaureate is as follows:</p>

<dd>"The International Baccalaureate aims to develop inquiring, knowledgeable, and caring young people who help to create a better and more peaceful world through intercultural understanding and respect. To this end, the IB works with schools, governments, and international organizations to develop challenging programmes of international education and rigorous assessment. These programmes encourage students across the world to become active, compassionate, and lifelong learners who understand that other people, with their differences, can also be right."</dd>

<h3>History</h3>
<hr>
<p>The programme was created by a group of international school educators (Kevin Bartlett of the Vienna International School, Paul Lieblich of Lyford Cay International School, Robert Landau of the Commonwealth American School of Lausanne, Susan Stengal of the Copenhagen International School and Peter Harding of the International School Hamburg, who wished to create a non-national based "best practice" educational framework for international schools. This group formed the International Schools Curriculum Project which received funding from original member schools and through the IBO from Shell Oil's international education division. After several years of development and increasing popularity the founding group decided to hand the "project" over to the IBO for management and continuing development.)</p>

<h3>Educational Philosophy</h3>
<hr>
<p>The philosophy of the PYP is to make the students into "inquirers, thinkers, communicators, risk takers, knowledgeable, principled, caring, open-minded, well-balanced, and reflective."</p>

<h3>Learner Profile</h3>
<hr>

<p>There are 10 attributes in the IB learner profile. PYP documents describe the PYP student profile as "the common ground on which PYP schools stand, the essence of what they are about" (Making the PYP Happen, 2000). Through the programmes the students should develop these traits. These traits originated in the PYP where it was called the "PYP student profle," but since the practitioners thought learning shouldn't come to a stop at age 11, they carried these attributes through to the completion of the diploma programmes; therefore, it's now called the "IB Learner Profile." The learner profile illustrates the qualities of an internationally minded person and a life long learner.</p>

<p>From the International Baccalaureate Organization 2007: Participant Workbook, Introduction to the PYP, the following is what IB learners strive to be:</p>
<ul>
<li>Inquirers: Students develop their natural curiosity.</li>
<li>Knowledgeable: Students explore concepts, ideas and issues that have both a local and global significance.</li>
<li>Thinkers: Students think critically to engage themselves in figuring out complex problems.</li>
<li>Communicators: Students express themselves and information through a variety of modes of communication.</li>
<li>Principled: Students act honestly and with a strong sense of fairness, justice, and respect for the dignity of the individual, groups, and communities.</li>
<li>Open-minded: Students appreciate their own cultures and personal histories and are open to the perspectives, values and traditions of other individuals and communities.</li>
<li>Caring: Students show respect and compassion towards the needs of others.</li>
<li>Risk-takers: Students approach unfamiliar situations with courage, as well as defend their beliefs.</li>
<li>Balanced: Students understand the importance of intellectual, physical and emotional balance to achieve personal well-being.</li>
<li>Reflective: Students give thoughtful consideration to their own learning and experience.</li>
</ul>


<h3>Transdisciplinary Themes</h3>
<hr>
<p>In the IBPYP the written curriculum is arranged within six "transdisciplinary themes". While the themes are universal and applicable to all cultures, the expectation is that there are certain core values, skills and knowledge for international schools, students and educators. For example, the international school curriculum should have “global significance—for all students in all cultures."</p>
<p><b>Who we are:</b> An inquiry into the nature of the self; beliefs and values; personal, physical, mental, social and spiritual health; human relationships including families, friends, communities, and cultures; rights and responsibilities; what it means to be human.</p>
<p><b>Where we are in place and time:</b> An inquiry into orientation in place and time; personal histories; homes and journeys; the discoveries, explorations and migrations of humankind; the relationships between and the interconnectedness of individuals and civilizations, from local and global perspectives.</p>
<p><b>How we express ourselves:</b> An inquiry into the ways in which we discover and express ideas, feelings, nature, culture, beliefs and values; the ways in which we reflect on, extend and enjoy our creativity; our appreciation of the aesthetic.</p>
<p><b>How the world works:</b> An inquiry into the natural world and its laws; the interaction between the natural world (physical and biological) and human societies; how humans use their understanding of scientific principles; the impact of scientific and technological advances on society and on the environment.</p>
<p><b>How we organize ourselves:</b> An inquiry into the interconnectedness of human-made systems and communities; the structure and function of organizations; societal decision-making; economic activities and their impact on humankind and the environment.</p>
<p><b>Sharing the planet:</b> An inquiry into rights and responsibilities in the struggle to share finite resources with other people and with other living things; communities and the relationships within and between them; access to equal opportunities; peace and conflict resolution.</p>


<h3>Participation</h3>
<hr>

<p>In order to participate in the IB Primary Years Programme, students must attend an authorised IB World School. "A PYP school is expected to implement the programme in an inclusive manner, so that all students in all the grades/year levels in the school or in the primary division of a school are engaged fully with the PYP." IB's requirement that the PYP must be implemented schoolwide has resulted in controversy in American public schools where parents cite lack of choice.</p>


