<p><b>Advanced Placement United States History (also known as AP U.S. History or APUSH)</b> is a course and examination offered by College Board as part of the Advanced Placement Program.</p>

<h3>Curriculum</h3>
<hr>
<p>The AP U.S. History course is designed to provide the same level of content and instruction that students would face in a freshman-level college survey class. AP U.S. History classes generally use a college-level textbook as the foundation for the course.</p>

<p><b>Textbooks</b></p>

<p>Commonly used textbooks that meet the curriculum requirements include:</p>
<ul>
<li>America's History (Henretta et al.)</li>
<li>American History: A Survey (Brinkley)</li>
<li>American Passages (Ayers et al.)</li>
<li>The American Pageant (Bailey et al.)</li>
<li>The American People (Nash et al.)</li>
<li>By the People (Fraser)</li>
<li>The Enduring Vision (Boyer et al.)</li>
<li>Give Me Liberty! (Foner)</li>
<li>Liberty, Equality, Power (Murrin et al.)</li>
<li>Out of Many (Faragher et al.)</li>
<li>A People and a Nation (Norton et al.)</li>
</ul>

<p><b>Criticism</b></p>
<p>American conservatives have criticised the curriculum for downplaying American exceptionalism and failing to foster patriotism. In 2014, there were protests against it in the Jefferson County Public Schools district in Colorado. In 2015, a bill to replace the curriculum was passed by the Oklahoma House of Representatives’ Education Committee, but later withdrawn.</p>


<h3>The Exam</h3>
<hr>
<p>The AP U.S. History exam lasts 3 hours and 15 minutes and consists of two sections; additionally, each section is divided into two parts. Section I, part A includes 55 multiple choice questions with each question containing four choices. The multiple choice questions cover American History from just before European contact with Native Americans to the present day. Moreover, section I, part B includes four short-answer questions. In total, students are given 105 minutes (55 for the multiple choice section and 50 for the four short-answer questions) to complete section I.</p>
<p>Section II of the exam is the free-response section, composed of a document-based question (DBQs) and one thematic essay. Section II, part A is composed of the DBQ, which provides an essay prompt and seven short primary sources or excerpts related to the prompt. Students are expected to write an essay responding to the prompt in which they utilize the sources in addition to outside information. Section II, part B consists of two essay prompts. Each thematic essay question on the AP exam may address any one of four possible historical thinking skills: analyzing continuity and change over time, comparisons, causation, or periodization. Students must respond to only one of the two essay prompts. Both of the essay questions will deal with events between the settlement of Jamestown (1607) and the election of Ronald Reagan (1980). There is a mandatory fifteen-minute reading period for students to read the essay prompts, take notes, and brainstorm; they may not begin to write the essays until this period has ended. Students will then have 90 minutes to write the two essays (60 for the DBQ and 30 for the thematic essay).</p>
<p>In May 2011, the AP U.S. History Test was taken by 402,947 students worldwide,  no longer making it the most-taken AP test; it has since been superseded by the AP English Language and Composition exam.</p>



<h3>Scoring</h3>
<hr>
<p>The AP U.S. History exam is divided into two sections. Section one consists of the multiple choice and short answer questions, while section two consists of the document-based question (DBQ) and a long essay question. Section one is worth 60% of the total AP exam score, with 40% of the total exam score derived from the student's performance on the multiple choice section and 20% of the total exam score derived from the student's performance on the short answer questions. The remaining 40% of the total exam score is derived from section two; the document-based question is worth 25% of the total exam score, while the long essay question is worth 15% of the total exam score.</p>



