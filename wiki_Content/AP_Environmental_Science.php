<p><b>Advanced Placement Environmental Science (AP Environmental Science, AP Viro, AP Environmental, AP Environment, AP Env. Sci., or APES)</b> is a course offered by the College Board as part of the Advanced Placement Program to high school students interested in the environmental and natural sciences. This course is designed to provide students with scientific principles, concepts, and methodologies necessary to comprehend the relationships abundant within the natural world, to identify and analyze environmental problems, to evaluate relative risks associated with these identified problems, and to examine alternative solutions for resolving and/or preventing similar problems facing the global environment. The percentage of students scoring a grade of "5" was only 10.4% in the 2009 testing administration. It remains one of the lowest "5" scoring AP Exams to this date right under AP Art History, AP English Literature & Composition, AP English Language & Composition, and AP World History.</p>


