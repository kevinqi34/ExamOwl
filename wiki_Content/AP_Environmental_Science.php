<h3>The Exam</h3>
<hr>
<ul>
<li><b>Section I</b>: Multiple Choice (100 questions, 90 minutes).</li>
<li><b>Section II</b>: Free-Response (one data-set question, one document-based question, and two synthesis and evaluation questions, 90 minutes).</li>
