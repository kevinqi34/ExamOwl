<p><b>AP Physics C: Electricity and Magnetism</b> is an Advanced Placement science course about electromagnetism. Methods of calculus are used wherever appropriate in formulating physical principles and in applying them to physical problems. It is supposed to be equivalent to an introductory college course in electricity and magnetism for physics majors. This course, taken along with courses covering other areas, such as mechanics, waves, thermodynamics, and modern physics can help prepare students for the SAT Subject Test in Physics, also administered by College Board. This course is often combined with AP Physics C: Mechanics to make a unified Physics C course that prepares for both exams, or it may be a separate course. In the former scenario, Electricity and Magnetism is typically taught second, as it requires much of the knowledge gained in the Mechanics course.</p>

<h3>The Exam</h3>
<hr>

<p>The exam is configured in two categories, a thirty-five (35) question multiple choice section and a three (3) question free response section. Test takers are allowed to use a calculator on the entire AP Physics C: Electricity & Magnetism exam – including both the multiple-choice and free response sections. The free response section permits the use of a calculator. The test is weighted such that each section is worth fifty percent (50%) of the final score. It is the shortest AP exam currently administered, with total time at 90 minutes.</p>
