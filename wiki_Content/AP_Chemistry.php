<h3>The Exam</h3>
<hr>

<p>The annual AP Chemistry examination, which is typically administered in May, is divided into two major sections (multiple-choice questions and free response essays).</p>
<p><b>Old test (2013 and earlier)</b></p>
<p>The old test was composed of two sections: a multiple-choice section consisting of 75 questions with five answer choices each, and a free-response section consisting of six essay prompts that required the authoring of chemical equations, solution of problems, and development of thoughtful essays in response to hypothetical scenarios.</p>

<ul>
<li><b>Section I, the multiple-choice portion</b>, did not allow the use of a calculator, nor did it provide any additional reference material, other than a periodic table. Each question contained five answer choices. 90 minutes were allotted for the completion of Section I. Section I covered the breadth of the curriculum.</li>
<li><b>Section II, the free response section</b>, was divided into two sections: Part A, requiring the completion of three problems, and Part B, also containing three problems. Part A, lasting 55 minutes, allowed the use of calculators, while Part B, lasting 40 minutes, did not. The first problem in Part A concerned equilibrium related to solubility, acids and bases, or pressure/concentration. The first question of Part B was a chemical equation question in which 3 scenarios were presented and the student was required to work all 3 scenarios, authoring a balanced net ionic chemical equation for each scenario and answering questions about the equations and scenarios. If time permitted, students may have edited their responses from Part A during the time allotted for responding to Part B, though without the use of a calculator. The student needed to have completed all six questions.</li>
</ul>

<p>While the use of calculators was prohibited during Section I and Section II Part B, a periodic table, a list of selected standard reduction potentials, and two pages of equations and conventions are available for use during the entirety of Section II.</p>

<p><b>New test (2014 and later)</b></p>

<p>The 2014 AP Chemistry exam was the first administration of a redesigned test as a result of a redesigning of the AP Chemistry course. The exam format is now different from the past years, with 60 multiple choice questions (now with only four answer choices per question), 3 long free response questions, and 4 short free response questions. The new exam has a focus on longer, more in depth, lab-based questions.</p>

