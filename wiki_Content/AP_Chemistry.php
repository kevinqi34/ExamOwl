<p><b>Advanced Placement Chemistry (AP Chemistry or AP Chem)</b> is a course and examination offered by the College Board as a part of the Advanced Placement Program to give American and Canadian high school students the opportunity to demonstrate their abilities and earn college-level credit.</p>





<h3>The Course</h3>
<hr>
<p>AP Chemistry is a course geared toward students with interests in chemical and physical sciences, as well as any of the biological sciences. The course aims to prepare students to take the AP Chemistry exam toward the end of the academic year. AP Chemistry topics include atomic theory, chemical bonding, phases of matter, solutions, types of reactions, chemical equilibrium, reaction kinetics, electrochemistry, and thermodynamics.</p>





<h3>Topics Covered</h3>
<hr>


<p>The exam covers common chemistry topics, including:</p>

	<ul>
				<li>Reactions</li>
					<ul>
						<li>Chemical equilibrium</li>
