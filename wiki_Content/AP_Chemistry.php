					<ul>
						<li>Atomic theory, including evidence for atomic theory</li>
						<li>Chemical bonding, including intermolecular forces (IMF)</li>
						<li>Nuclear chemistry (removed for May 2014 test)</li>
						<li>Molecular Geometry</li>
						<li>Molecular Models</li>
