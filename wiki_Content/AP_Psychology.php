<p><b>Advanced Placement Psychology (AP Psychology, or AP Psych)</b> course and corresponding exam is part of College Board's Advanced Placement Program. This course is tailored for students interested in the field of psychology and as an opportunity to earn Advanced Placement credit or exemption from a college-level psychology course. It was the shortest AP exam until AP Physics C exam was split into two separate exams in 2006.</p>

