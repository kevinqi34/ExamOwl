<h3>The Exam</h3>
<hr>

<p>The exam includes two sections: a 70 minute multiple choice section (100 questions) and a 50 minute free response section (2 prompts). The multiple choice provides two-thirds of the grade and the free-response provides the remaining third.</p>

<p>Beginning with the May 2011 AP Exam administration, total scores on the multiple-choice section are based only on the number of questions answered correctly. Points are no longer deducted for incorrect answers. Grading (the number of points needed to get a certain score) is slightly more strict as a result.</p>








