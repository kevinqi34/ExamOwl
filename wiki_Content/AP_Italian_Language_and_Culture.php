<p><b>AP Italian Language and Culture (or AP Italian)</b> is a course offered by the American College Board as part of the Advanced Placement Program. It is intended to give students a thorough background in the Italian language and Italian culture equivalent to a college-level course.</p>
<p>Due to low numbers of students taking AP Italian, it was temporarily discontinued after the 2008-2009 year. On July 3, 2008, The Italian Language Foundation was established to support Italian language education and the AP Italian program. On November 10, 2010 the College Board announced that the program will be reinstated in the fall of 2011, with the first AP Italian Exam scheduled for May 2012.</p>


<h3>Course Content</h3>
<hr>
<p>The AP Italian Language and Culture course focuses on developing students' reading, writing, listening, and speaking skills, all framed in order to reflect the richness of Italian language and culture. Teachers of the course will interweave the language structure with cultural content.</p>

<h3>The Exam</h3>
<hr>
<p>The examination tests students' abilities to successfully use three modes of communication: interpersonal, interpretive, and presentational. The five ultimate goals of the exam are communication, culture, connections, comparisons, and community.</p>


<h3>Grade Distribution</h3>
<hr>
<p>In the 2012 administration, 1,806 students took the exam, with a mean score of 3.37. 1,300 students indicated themselves as non-native speakers, who did not use Italian on a regular basis. The mean score for this group was 3.06.</p>
