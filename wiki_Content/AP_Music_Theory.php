<p><b>Advanced Placement Music Theory (or AP Music Theory, AP Jams, AP Music, or even Music AP)</b> is a course and examination offered in the United States by the College Board as part of the Advanced Placement Program to high school students who wish to earn credit for a college level music theory course.</p>

<h3>The Course</h3>
<hr>
<p>Some of the material covered in the course involves low sight reading, in-depth terminology, musical phrasing and composition, music history, chord structure and cadences, and other areas of music theory. This course is recommended for students with particularly strong abilities in music and college music majors.</p>

<h3>The Exam</h3>
<hr>
<p>The exam itself is divided into two broad sections: Section I, the multiple-choice section, and Section II, the free-response section. In turn, each section is divided into parts.</p>
<p><b>Section I - Multiple Choice</b></p>
<p>Section I of the exam consists of four-choice multiple choice questions; the total amount varies each year. The first half of the section is listening-based; the proctor will begin playing a provided CD, and the exam will begin. Each question or group of questions is based on a musical selection or an auditory stimulus. The selection or stimulus will be played, and the student must answer as many of the questions as possible. Each musical selection or auditory stimulus is usually played two or three times for each question or group of questions, though the exact number differs from question to question.</p>


<p><b>Section II - Free Response</b></p>
<p>Section II of the exam consists of three different parts, all of which are student-produced responses. One part of the section is listening-based, one part is part-writing, and one part is sight-singing.</p>
<p>The listening-based part of the section contains two types of questions: melodic dictation, in which a one-part melody is played two or three times, a starting pitch, time signature, and key are given on the answer sheet, and the student must accurately record both the pitch and length of the played notes. The student must record two melodies; generally, one melody is in a major key and in a compound meter while the other melody is in a minor key and in a simple meter. One melody is written in the treble clef and the other is in the bass clef. The first melodic dictation question is in simple meter, while the second melodic question is in compound time. The reason for this is to test candidate AP Music Theory students in their ability to distinguish between simple and compound time signatures as well as being able to read bass clef and treble clef.</p>
<p>The second type of listening-based question is harmonic dictation. A four-part texture, traditional SATB, is played four times. The key, starting pitch for each part, and time signature are given on the answer sheet. The student must accurately dictate only the bass and soprano lines, though the bass, tenor, alto, and soprano parts are being played on the recording. The student must also provide a Roman numeral analysis of the chords played in the progression played.</p>
<p>The part-writing part of section II requires that the student, using knowledge of "eighteenth century composition guidelines" (as indicated on CollegeBoard's latest rubric and on the released 2003-2006 exams on AP Central), "standard" chord progressions, cadences, voicing, and part ranges, write a short (usually two or four measure) two- or four-part texture given starting pitches, a key, time signature, and another piece of information that varies with each question: figured bass notation and completed bass part, Roman numeral notation, or a completed soprano line.</p>
<p>For the question with the supplied figured bass notation and completed bass line, the student must write the Roman numeral notation of each chord and fill in the remaining tenor, alto, and soprano lines. For the question with the supplied Roman numeral notation, the student must write all four parts of the texture. For the question with the supplied soprano line, the student must develop a chord progression based on the given soprano line and write only an accompanying bass part.</p>
<p>The sight-singing part of the exam requires the student to analyze a given melodic line and perform the line as accurately as possible. Students perform two melodies one at a time; the written starting pitch will be played, and the student will have 75 seconds to practice aloud any parts he or she desires. Once the 75 seconds have ended, the starting pitch will be played again, and the student will have 30 seconds to perform the piece. Transposition of the key is allowed for voice comfortability as stated on each year's directions. As with the melodic dictation part of the exam, one piece will generally be compound in meter and major in key, while the other will be simple in meter and minor in key. Students may sing the melody using solfège syllables, numbers, or a random syllable (la,la,la...ta,ta,ta etc...)</p>

